pub fn vault_bridge_receipts(
    options: VaultBridgeReceiptsOptions,
) -> io::Result<VaultBridgeReceiptsReport> {
    let asset_id = options.asset_id.clone();
    let bucket_id = options.bucket_id.clone();
    let status = vault_bridge_status(VaultBridgeStatusOptions {
        data_dir: options.data_dir,
        asset_id,
    })?;
    let receipts = status
        .receipts
        .into_iter()
        .filter(|receipt| {
            bucket_id
                .as_ref()
                .is_none_or(|bucket_id| &receipt.bucket_id == bucket_id)
        })
        .collect::<Vec<_>>();
    Ok(VaultBridgeReceiptsReport {
        schema: VAULT_BRIDGE_RECEIPTS_REPORT_SCHEMA.to_string(),
        asset_id: status.asset_id,
        bucket_id,
        receipt_count: receipts.len() as u64,
        receipts,
        disclosure: VAULT_BRIDGE_STATUS_DISCLOSURE.to_string(),
    })
}

