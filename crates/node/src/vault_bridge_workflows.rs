fn validate_vault_bridge_deposit_filters(
    evidence: &VaultBridgeDepositEvidence,
    vault_address: Option<&str>,
    token_address: Option<&str>,
) -> Result<(), String> {
    if let Some(vault_address) = vault_address {
        let expected_vault =
            vault_bridge_normalized_evm_address_text("--vault-address", vault_address)?;
        if evidence.vault_address != expected_vault {
            return Err("vault bridge deposit evidence does not match --vault-address".to_string());
        }
    }
    if let Some(token_address) = token_address {
        let expected_token =
            vault_bridge_normalized_evm_address_text("--token-address", token_address)?;
        if evidence.token_address != expected_token {
            return Err("vault bridge deposit evidence does not match --token-address".to_string());
        }
    }
    Ok(())
}

fn vault_bridge_deposit_plan_log_input(
    options: &VaultBridgeDepositPlanOptions,
) -> io::Result<serde_json::Value> {
    match (&options.log_file, &options.receipt_file) {
        (Some(log_file), None) => read_json_file(log_file, "vault bridge asset vault deposit log"),
        (None, Some(receipt_file)) => {
            let receipt: serde_json::Value =
                read_json_file(receipt_file, "vault bridge asset vault deposit receipt")?;
            select_vault_bridge_deposit_log_from_receipt(
                &receipt,
                options.vault_address.as_deref(),
                options.token_address.as_deref(),
            )
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))
        }
        (None, None) => Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "vault bridge deposit plan requires --log-file or --receipt-file",
        )),
        (Some(_), Some(_)) => Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "vault bridge deposit plan accepts only one of --log-file or --receipt-file",
        )),
    }
}

fn select_vault_bridge_deposit_log_from_receipt(
    receipt: &serde_json::Value,
    vault_address: Option<&str>,
    token_address: Option<&str>,
) -> Result<serde_json::Value, String> {
    let expected_vault = vault_address
        .map(|address| vault_bridge_normalized_evm_address_text("--vault-address", address))
        .transpose()?;
    let expected_token = token_address
        .map(|address| vault_bridge_normalized_evm_address_text("--token-address", address))
        .transpose()?;
    let logs = receipt
        .get("logs")
        .and_then(serde_json::Value::as_array)
        .ok_or_else(|| "vault bridge asset receipt missing logs array".to_string())?;
    let receipt_block_hash =
        vault_bridge_optional_log_string(receipt, &["blockHash", "block_hash"]);
    let receipt_tx_hash =
        vault_bridge_optional_log_string(receipt, &["transactionHash", "txHash", "tx_hash"]);

    let mut selected: Option<serde_json::Value> = None;
    for log in logs {
        let Some(topics) = log.get("topics").and_then(serde_json::Value::as_array) else {
            continue;
        };
        let Some(topic0) = topics.first() else {
            continue;
        };
        let Ok(topic0) = vault_bridge_normalized_hex_json("topics[0]", topic0, EVM_ABI_WORD_BYTES)
        else {
            continue;
        };
        if topic0 != VAULT_BRIDGE_VAULT_DEPOSIT_EVENT_TOPIC
            && topic0 != VAULT_BRIDGE_VAULT_DEPOSIT_V2_EVENT_TOPIC
        {
            continue;
        }
        if let Some(expected_vault) = expected_vault.as_deref() {
            let log_address = vault_bridge_log_string(log, &["address", "contractAddress"])
                .and_then(|address| vault_bridge_normalized_evm_address_text("address", address))?;
            if log_address != expected_vault {
                continue;
            }
        }

        let mut candidate = log.clone();
        if let Some(candidate_object) = candidate.as_object_mut() {
            if !candidate_object.contains_key("blockHash") {
                if let Some(block_hash) = receipt_block_hash {
                    candidate_object.insert(
                        "blockHash".to_string(),
                        serde_json::Value::String(block_hash.to_string()),
                    );
                }
            }
            if !candidate_object.contains_key("transactionHash") {
                if let Some(tx_hash) = receipt_tx_hash {
                    candidate_object.insert(
                        "transactionHash".to_string(),
                        serde_json::Value::String(tx_hash.to_string()),
                    );
                }
            }
        }
        let evidence = parse_vault_bridge_vault_deposit_log(&candidate)?;
        if let Some(expected_token) = expected_token.as_deref() {
            if evidence.token_address != expected_token {
                continue;
            }
        }
        if selected.replace(candidate).is_some() {
            return Err(
                "vault bridge asset receipt contains multiple matching ERC20BridgeDeposited logs; provide stricter --vault-address/--token-address filters"
                    .to_string(),
            );
        }
    }

    selected.ok_or_else(|| {
        "vault bridge asset receipt contains no matching ERC20BridgeDeposited log".to_string()
    })
}

fn parse_vault_bridge_vault_deposit_log(
    log: &serde_json::Value,
) -> Result<VaultBridgeDepositEvidence, String> {
    let topics = vault_bridge_log_array(log, "topics")?;
    if topics.len() != 4 {
        return Err("vault bridge asset vault deposit log must have exactly 4 topics".to_string());
    }
    let topic0 = vault_bridge_normalized_hex_json("topics[0]", &topics[0], EVM_ABI_WORD_BYTES)?;
    let is_v2 = topic0 == VAULT_BRIDGE_VAULT_DEPOSIT_V2_EVENT_TOPIC;
    if topic0 != VAULT_BRIDGE_VAULT_DEPOSIT_EVENT_TOPIC && !is_v2 {
        return Err(
            "vault bridge asset vault deposit log topic0 is not a supported ERC20BridgeDeposited event"
                .to_string(),
        );
    }
    let deposit_id = vault_bridge_normalized_hex_json("topics[1]", &topics[1], EVM_ABI_WORD_BYTES)?;
    let depositor = vault_bridge_indexed_address_topic("topics[2]", &topics[2])?;
    let pftl_recipient_hash =
        vault_bridge_normalized_hex_json("topics[3]", &topics[3], EVM_ABI_WORD_BYTES)?;

    let data_hex = vault_bridge_log_string(log, &["data"])?;
    let data = vault_bridge_hex_bytes("data", data_hex)?;
    let head_words = if is_v2 {
        VAULT_BRIDGE_VAULT_DEPOSIT_V2_ABI_HEAD_WORDS
    } else {
        VAULT_BRIDGE_VAULT_DEPOSIT_ABI_HEAD_WORDS
    };
    let minimum_head = head_words
        .checked_mul(EVM_ABI_WORD_BYTES)
        .ok_or_else(|| "vault bridge asset vault deposit ABI head length overflow".to_string())?;
    if data.len() < minimum_head {
        return Err("vault bridge asset vault deposit data is shorter than ABI head".to_string());
    }

    let recipient_offset = vault_bridge_abi_word_u64(&data, 0, "data.pftl_recipient_offset")?;
    let pftl_recipient =
        vault_bridge_abi_dynamic_string(&data, recipient_offset, "data.pftl_recipient")?;
    let amount_atoms = vault_bridge_abi_word_u64(&data, 1, "data.amount")?;
    let nonce = vault_bridge_abi_word_hex(&data, 2, "data.nonce")?;
    let route_binding = if is_v2 {
        vault_bridge_abi_word_hex(&data, 3, "data.route_binding")?
    } else {
        String::new()
    };
    let source_chain_id_word = if is_v2 { 4 } else { 3 };
    let source_chain_id =
        vault_bridge_abi_word_u64(&data, source_chain_id_word, "data.source_chain_id")?;
    let vault_address =
        vault_bridge_abi_word_address(&data, source_chain_id_word + 1, "data.vault")?;
    let token_address =
        vault_bridge_abi_word_address(&data, source_chain_id_word + 2, "data.token")?;

    let log_address = vault_bridge_normalized_evm_address_text(
        "address",
        vault_bridge_log_string(log, &["address", "contractAddress"])?,
    )?;
    if log_address != vault_address {
        return Err(
            "vault bridge asset vault deposit log address does not match event vault".to_string(),
        );
    }

    let block_hash = vault_bridge_normalized_hex_text(
        "blockHash",
        vault_bridge_log_string(log, &["blockHash", "block_hash"])?,
        EVM_ABI_WORD_BYTES,
    )?;
    let tx_hash = vault_bridge_normalized_hex_text(
        "transactionHash",
        vault_bridge_log_string(log, &["transactionHash", "txHash", "tx_hash"])?,
        EVM_ABI_WORD_BYTES,
    )?;
    let log_index = vault_bridge_log_u64(log, &["logIndex", "log_index"])?;

    let evidence = VaultBridgeDepositEvidence {
        source_chain_id,
        vault_address,
        token_address,
        depositor,
        pftl_recipient,
        pftl_recipient_hash,
        amount_atoms,
        nonce,
        route_binding,
        deposit_id,
        block_hash,
        tx_hash,
        log_index,
    };
    evidence.validate()?;
    let expected_deposit_id = vault_bridge_deposit_id(&evidence)?;
    if evidence.deposit_id != expected_deposit_id {
        return Err(
            "vault bridge asset vault deposit log deposit_id does not match event preimage"
                .to_string(),
        );
    }
    Ok(evidence)
}

fn vault_bridge_log_array<'a>(
    log: &'a serde_json::Value,
    field: &str,
) -> Result<&'a Vec<serde_json::Value>, String> {
    log.get(field)
        .and_then(serde_json::Value::as_array)
        .ok_or_else(|| {
            format!("vault bridge asset vault deposit log missing array field `{field}`")
        })
}

fn vault_bridge_log_string<'a>(
    log: &'a serde_json::Value,
    fields: &[&str],
) -> Result<&'a str, String> {
    for field in fields {
        if let Some(value) = log.get(*field).and_then(serde_json::Value::as_str) {
            return Ok(value);
        }
    }
    Err(format!(
        "vault bridge asset vault deposit log missing string field `{}`",
        fields.join("|")
    ))
}

fn vault_bridge_optional_log_string<'a>(
    value: &'a serde_json::Value,
    fields: &[&str],
) -> Option<&'a str> {
    fields
        .iter()
        .find_map(|field| value.get(*field).and_then(serde_json::Value::as_str))
}

fn vault_bridge_log_u64(log: &serde_json::Value, fields: &[&str]) -> Result<u64, String> {
    for field in fields {
        if let Some(value) = log.get(*field) {
            return vault_bridge_json_u64(field, value);
        }
    }
    Err(format!(
        "vault bridge asset vault deposit log missing u64 field `{}`",
        fields.join("|")
    ))
}

fn vault_bridge_json_u64(field: &str, value: &serde_json::Value) -> Result<u64, String> {
    if let Some(number) = value.as_u64() {
        return Ok(number);
    }
    let Some(text) = value.as_str() else {
        return Err(format!("{field} must be a u64 number or string"));
    };
    let trimmed = text.trim();
    if let Some(hex) = trimmed
        .strip_prefix("0x")
        .or_else(|| trimmed.strip_prefix("0X"))
    {
        return u64::from_str_radix(hex, 16).map_err(|_| format!("{field} is not a u64 hex"));
    }
    trimmed
        .parse::<u64>()
        .map_err(|_| format!("{field} is not a u64 decimal"))
}

fn vault_bridge_normalized_hex_json(
    field: &str,
    value: &serde_json::Value,
    expected_bytes: usize,
) -> Result<String, String> {
    let Some(text) = value.as_str() else {
        return Err(format!("{field} must be a hex string"));
    };
    vault_bridge_normalized_hex_text(field, text, expected_bytes)
}

fn vault_bridge_normalized_hex_text(
    field: &str,
    value: &str,
    expected_bytes: usize,
) -> Result<String, String> {
    let bytes = vault_bridge_hex_bytes_exact(field, value, expected_bytes)?;
    Ok(bytes_to_hex(&bytes))
}

fn vault_bridge_hex_bytes(field: &str, value: &str) -> Result<Vec<u8>, String> {
    let stripped = value
        .trim()
        .strip_prefix("0x")
        .or_else(|| value.trim().strip_prefix("0X"))
        .unwrap_or_else(|| value.trim());
    hex_to_bytes(stripped).map_err(|error| format!("{field} has invalid hex: {error}"))
}

pub(super) fn vault_bridge_hex_bytes_exact(
    field: &str,
    value: &str,
    expected_bytes: usize,
) -> Result<Vec<u8>, String> {
    let bytes = vault_bridge_hex_bytes(field, value)?;
    if bytes.len() != expected_bytes {
        return Err(format!(
            "{field} must be {expected_bytes} bytes, got {}",
            bytes.len()
        ));
    }
    Ok(bytes)
}

fn vault_bridge_normalized_evm_address_text(field: &str, value: &str) -> Result<String, String> {
    let bytes = vault_bridge_hex_bytes_exact(field, value, 20)?;
    if bytes.iter().all(|byte| *byte == 0) {
        return Err(format!("{field} must not be the zero EVM address"));
    }
    Ok(format!("0x{}", bytes_to_hex(&bytes)))
}

fn vault_bridge_indexed_address_topic(
    field: &str,
    value: &serde_json::Value,
) -> Result<String, String> {
    let topic = vault_bridge_hex_bytes_exact(
        field,
        value
            .as_str()
            .ok_or_else(|| format!("{field} must be a hex string"))?,
        EVM_ABI_WORD_BYTES,
    )?;
    if topic[..12].iter().any(|byte| *byte != 0) {
        return Err(format!("{field} indexed address has nonzero left padding"));
    }
    if topic[12..].iter().all(|byte| *byte == 0) {
        return Err(format!("{field} indexed address must not be zero"));
    }
    Ok(format!("0x{}", bytes_to_hex(&topic[12..])))
}

fn vault_bridge_abi_word<'a>(
    data: &'a [u8],
    index: usize,
    field: &str,
) -> Result<&'a [u8], String> {
    let start = index
        .checked_mul(EVM_ABI_WORD_BYTES)
        .ok_or_else(|| format!("{field} ABI index overflow"))?;
    let end = start
        .checked_add(EVM_ABI_WORD_BYTES)
        .ok_or_else(|| format!("{field} ABI end overflow"))?;
    data.get(start..end)
        .ok_or_else(|| format!("{field} ABI word out of bounds"))
}

fn vault_bridge_abi_word_u64(data: &[u8], index: usize, field: &str) -> Result<u64, String> {
    let word = vault_bridge_abi_word(data, index, field)?;
    if word[..24].iter().any(|byte| *byte != 0) {
        return Err(format!("{field} exceeds u64 range"));
    }
    let mut out = [0_u8; 8];
    out.copy_from_slice(&word[24..]);
    Ok(u64::from_be_bytes(out))
}

fn vault_bridge_abi_word_hex(data: &[u8], index: usize, field: &str) -> Result<String, String> {
    let word = vault_bridge_abi_word(data, index, field)?;
    if word.iter().all(|byte| *byte == 0) {
        return Err(format!("{field} must not be zero"));
    }
    Ok(bytes_to_hex(word))
}

fn vault_bridge_abi_word_address(data: &[u8], index: usize, field: &str) -> Result<String, String> {
    let word = vault_bridge_abi_word(data, index, field)?;
    if word[..12].iter().any(|byte| *byte != 0) {
        return Err(format!("{field} address has nonzero left padding"));
    }
    if word[12..].iter().all(|byte| *byte == 0) {
        return Err(format!("{field} address must not be zero"));
    }
    Ok(format!("0x{}", bytes_to_hex(&word[12..])))
}

fn vault_bridge_abi_dynamic_string(
    data: &[u8],
    offset: u64,
    field: &str,
) -> Result<String, String> {
    let offset = usize::try_from(offset).map_err(|_| format!("{field} offset too large"))?;
    if !offset.is_multiple_of(EVM_ABI_WORD_BYTES) {
        return Err(format!("{field} offset is not word-aligned"));
    }
    let minimum_head = VAULT_BRIDGE_VAULT_DEPOSIT_ABI_HEAD_WORDS * EVM_ABI_WORD_BYTES;
    if offset < minimum_head {
        return Err(format!("{field} offset points into ABI head"));
    }
    let length_index = offset
        .checked_div(EVM_ABI_WORD_BYTES)
        .ok_or_else(|| format!("{field} offset division failed"))?;
    let length = vault_bridge_abi_word_u64(data, length_index, &format!("{field}.length"))?;
    let length = usize::try_from(length).map_err(|_| format!("{field} length too large"))?;
    if length == 0 {
        return Err(format!("{field} must not be empty"));
    }
    if length > MAX_VAULT_BRIDGE_LOG_RECIPIENT_BYTES {
        return Err(format!(
            "{field} exceeds {MAX_VAULT_BRIDGE_LOG_RECIPIENT_BYTES} bytes"
        ));
    }
    let value_start = offset
        .checked_add(EVM_ABI_WORD_BYTES)
        .ok_or_else(|| format!("{field} value start overflow"))?;
    let value_end = value_start
        .checked_add(length)
        .ok_or_else(|| format!("{field} value end overflow"))?;
    let padded_length = length
        .checked_add(EVM_ABI_WORD_BYTES - 1)
        .and_then(|value| value.checked_div(EVM_ABI_WORD_BYTES))
        .and_then(|words| words.checked_mul(EVM_ABI_WORD_BYTES))
        .ok_or_else(|| format!("{field} padded length overflow"))?;
    let expected_end = value_start
        .checked_add(padded_length)
        .ok_or_else(|| format!("{field} padded end overflow"))?;
    if data.len() != expected_end {
        return Err(format!("{field} ABI data has trailing or truncated bytes"));
    }
    let padding = data
        .get(value_end..expected_end)
        .ok_or_else(|| format!("{field} padding out of bounds"))?;
    if padding.iter().any(|byte| *byte != 0) {
        return Err(format!("{field} ABI string padding is nonzero"));
    }
    let value = data
        .get(value_start..value_end)
        .ok_or_else(|| format!("{field} value out of bounds"))?;
    String::from_utf8(value.to_vec()).map_err(|_| format!("{field} is not UTF-8"))
}

