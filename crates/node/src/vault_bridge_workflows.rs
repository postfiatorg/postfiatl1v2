fn replay_vault_bridge_reserve_bundle_data(
    bundle: &VaultBridgeReserveReplayBundle,
) -> Result<(String, u64, u64), String> {
    if bundle.schema != VAULT_BRIDGE_RESERVE_REPLAY_BUNDLE_SCHEMA {
        return Err(format!(
            "vault bridge asset reserve replay bundle schema must be `{VAULT_BRIDGE_RESERVE_REPLAY_BUNDLE_SCHEMA}`, got `{}`",
            bundle.schema
        ));
    }
    bundle.asset_definition.validate()?;
    bundle.nav_asset.validate()?;
    bundle.reserve_packet.validate()?;
    if bundle.asset_definition.asset_id != bundle.asset_id {
        return Err(
            "vault bridge asset replay asset definition does not match bundle asset_id".to_string(),
        );
    }
    if bundle.nav_asset.asset_id != bundle.asset_id {
        return Err(
            "vault bridge asset replay NAV asset does not match bundle asset_id".to_string(),
        );
    }
    if bundle.reserve_packet.asset_id != bundle.asset_id {
        return Err(
            "vault bridge asset replay reserve packet does not match bundle asset_id".to_string(),
        );
    }
    if bundle.reserve_packet.epoch != bundle.epoch {
        return Err("vault bridge asset replay reserve packet epoch mismatch".to_string());
    }
    if bundle.reserve_packet.state != NAV_RESERVE_STATE_FINALIZED {
        return Err("vault bridge asset replay reserve packet must be finalized".to_string());
    }
    if bundle.expected_reserve_packet_hash != bundle.reserve_packet.reserve_packet_hash {
        return Err("vault bridge asset replay expected reserve packet hash mismatch".to_string());
    }
    if bundle.nav_asset.finalized_epoch == bundle.epoch {
        if bundle.nav_asset.finalized_reserve_packet_hash
            != bundle.reserve_packet.reserve_packet_hash
        {
            return Err(
                "vault bridge asset replay NAV asset finalized hash does not match reserve packet"
                    .to_string(),
            );
        }
        if bundle.nav_asset.nav_per_unit != bundle.reserve_packet.nav_per_unit {
            return Err(
                "vault bridge asset replay NAV asset nav_per_unit does not match reserve packet"
                    .to_string(),
            );
        }
        if bundle.nav_asset.circulating_supply != bundle.reserve_packet.circulating_supply {
            return Err(
                "vault bridge asset replay NAV asset circulating supply does not match reserve packet"
                    .to_string(),
            );
        }
    }

    let mut ledger = LedgerState::new(Vec::new());
    ledger
        .asset_definitions
        .push(bundle.asset_definition.clone());
    ledger.nav_assets.push(bundle.nav_asset.clone());
    ledger.trustlines = bundle.trustlines.clone();
    ledger.escrows = bundle.escrows.clone();
    ledger.offers = bundle.offers.clone();
    ledger.fast_lane_reserves = bundle.fast_lane_reserves.clone();
    ledger.pftl_uniswap_routes = bundle.pftl_uniswap_routes.clone();
    ledger
        .nav_reserve_packets
        .push(bundle.reserve_packet.clone());
    ledger.vault_bridge_bucket_states = bundle.buckets.clone();
    ledger.vault_bridge_receipts = bundle.receipts.clone();
    ledger.vault_bridge_allocations = bundle.allocations.clone();
    ledger.vault_bridge_redemptions = bundle.redemptions.clone();
    ledger.validate_asset_state(&bundle.chain_id)?;
    let mut shielded = ShieldedState::empty();
    if !bundle.asset_orchard_balances.is_empty() {
        let mut pool = OrchardPoolState::empty("vault-bridge-reserve-replay");
        pool.asset_orchard_balances = bundle.asset_orchard_balances.clone();
        shielded.orchard = Some(pool);
    }

    let source_root = vault_bridge_source_root_for_asset(&bundle.buckets, &bundle.asset_id)?;
    if source_root != bundle.expected_source_root {
        return Err(
            "vault bridge asset replay source root does not match exported expectation".to_string(),
        );
    }
    if source_root != bundle.reserve_packet.source_root {
        return Err(
            "vault bridge asset replay source root does not match reserve packet".to_string(),
        );
    }
    let counted_value_atoms =
        vault_bridge_counted_value_for_asset(&bundle.buckets, &bundle.asset_id)?;
    if counted_value_atoms != bundle.expected_counted_value_atoms {
        return Err(
            "vault bridge asset replay counted value does not match exported expectation"
                .to_string(),
        );
    }
    if counted_value_atoms != bundle.reserve_packet.verified_net_assets {
        return Err(
            "vault bridge asset replay counted value does not match reserve packet".to_string(),
        );
    }
    let issued_supply_atoms = global_issued_asset_supply(&ledger, &shielded, &bundle.asset_id)
        .map_err(|error| error.to_string())?;
    if issued_supply_atoms != bundle.expected_issued_supply_atoms {
        return Err(
            "vault bridge asset replay issued supply does not match exported expectation"
                .to_string(),
        );
    }
    if issued_supply_atoms != bundle.reserve_packet.circulating_supply {
        return Err(
            "vault bridge asset replay issued supply does not match reserve packet".to_string(),
        );
    }
    Ok((source_root, counted_value_atoms, issued_supply_atoms))
}

pub(super) fn replay_market_ops_bundle_data(
    bundle: &MarketOpsReplayBundle,
) -> Result<String, String> {
    if bundle.schema != MARKET_OPS_REPLAY_BUNDLE_SCHEMA {
        return Err(format!(
            "market ops replay bundle schema must be `{MARKET_OPS_REPLAY_BUNDLE_SCHEMA}`, got `{}`",
            bundle.schema
        ));
    }
    bundle.policy_inputs.validate()?;
    bundle.envelope.validate_basic()?;
    if bundle.asset_id != bundle.reserve_packet.asset_id {
        return Err("market ops replay bundle asset_id does not match reserve packet".to_string());
    }
    if bundle.epoch != bundle.reserve_packet.epoch || bundle.epoch != bundle.envelope.epoch {
        return Err("market ops replay bundle epoch mismatch".to_string());
    }
    if bundle.reserve_packet.state != NAV_RESERVE_STATE_FINALIZED {
        return Err("market ops replay reserve packet must be finalized".to_string());
    }
    let expected = recompute_market_ops_replay_envelope(
        &bundle.asset_id,
        &bundle.reserve_packet,
        &bundle.envelope,
        &bundle.policy_inputs,
    )?;
    if expected != bundle.envelope {
        return Err(
            "market ops replay envelope does not match deterministic policy output".to_string(),
        );
    }
    if bundle.reserve_packet_hash != bytes_to_hex(&bundle.envelope.reserve_packet_hash) {
        return Err("market ops replay reserve_packet_hash field mismatch".to_string());
    }
    if bundle.supply_packet_hash != bytes_to_hex(&bundle.envelope.supply_packet_hash) {
        return Err("market ops replay supply_packet_hash field mismatch".to_string());
    }
    if bundle.evidence_root != bytes_to_hex(&bundle.envelope.evidence_root) {
        return Err("market ops replay evidence_root field mismatch".to_string());
    }
    if bundle.program_id != bytes_to_hex(&bundle.envelope.program_id) {
        return Err("market ops replay program_id field mismatch".to_string());
    }
    if bundle.policy_hash != bytes_to_hex(&bundle.envelope.policy_hash) {
        return Err("market ops replay policy_hash field mismatch".to_string());
    }
    if bundle.parameter_hash != bytes_to_hex(&bundle.envelope.parameter_hash) {
        return Err("market ops replay parameter_hash field mismatch".to_string());
    }
    if bundle.previous_market_state_hash
        != bytes_to_hex(&bundle.envelope.previous_market_state_hash)
    {
        return Err("market ops replay previous_market_state_hash field mismatch".to_string());
    }
    let computed_envelope_hash = bytes_to_hex(&bundle.envelope.envelope_hash());
    if computed_envelope_hash != bundle.expected_envelope_hash {
        return Err(format!(
            "market ops replay envelope_hash mismatch: computed {computed_envelope_hash}, expected {}",
            bundle.expected_envelope_hash
        ));
    }
    Ok(computed_envelope_hash)
}

pub(super) fn recompute_market_ops_replay_envelope(
    asset_id: &str,
    packet: &NavReservePacket,
    envelope: &MarketOpsEnvelope,
    inputs: &MarketOpsPolicyInputs,
) -> Result<MarketOpsEnvelope, String> {
    let valid_global_supply_atoms = u128::from(packet.circulating_supply);
    let verified_net_assets_usd_e8 = u128::from(packet.verified_net_assets);
    let nav_floor = postfiat_execution::market_policy::compute_nav_floor_with_unit_scale(
        verified_net_assets_usd_e8,
        valid_global_supply_atoms,
        u128::from(inputs.floor_factor_bps),
        inputs.unit_scale,
    )
    .map_err(|error| format!("{error}"))?;
    let backing_capacity =
        postfiat_execution::market_policy::compute_backing_capacity_with_unit_scale(
            verified_net_assets_usd_e8,
            valid_global_supply_atoms,
            nav_floor.nav_floor_usd_e8,
            inputs.unit_scale,
        )
        .map_err(|error| format!("{error}"))?;
    let portfolio_floor_value_usd_e8 = postfiat_execution::market_policy::mul_div_floor(
        valid_global_supply_atoms,
        nav_floor.nav_floor_usd_e8,
        inputs.unit_scale,
    )
    .map_err(|error| format!("{error}"))?;
    let alignment = postfiat_execution::market_policy::compute_alignment_reserve_requirement(
        portfolio_floor_value_usd_e8,
        &inputs.cost_to_restore_14d_usd_e8,
        &inputs.cost_to_restore_90d_usd_e8,
        postfiat_execution::market_policy::AlignmentReserveParams {
            policy_min_usd_e8: inputs.alignment_params.policy_min_usd_e8,
            min_alignment_bps: u128::from(inputs.alignment_params.min_alignment_bps),
            stress_repeat_factor_14d: inputs.alignment_params.stress_repeat_factor_14d,
            stress_repeat_factor_90d: inputs.alignment_params.stress_repeat_factor_90d,
            stale_epochs_allowed: inputs.alignment_params.stale_epochs_allowed,
            max_decay_per_epoch_bps: u128::from(inputs.alignment_params.max_decay_per_epoch_bps),
        },
        inputs.previous_required_alignment_reserve_usd_e8,
    )
    .map_err(|error| format!("{error}"))?;
    let evidence_root =
        market_ops_evidence_root(&inputs.discount_observations, &inputs.premium_observations)?;
    let window_seconds = envelope
        .data_window_end
        .checked_sub(envelope.data_window_start)
        .ok_or_else(|| "market ops replay data window underflows".to_string())?;
    let discount_observations = inputs
        .discount_observations
        .iter()
        .map(
            |observation| postfiat_execution::market_policy::VenueObservation {
                dt_seconds: u128::from(observation.dt_seconds),
                price_usd_e8: observation.price_usd_e8,
                volume_usd_e8: observation.volume_usd_e8,
            },
        )
        .collect::<Vec<_>>();
    let premium_observations = inputs
        .premium_observations
        .iter()
        .map(
            |observation| postfiat_execution::market_policy::VenueObservation {
                dt_seconds: u128::from(observation.dt_seconds),
                price_usd_e8: observation.price_usd_e8,
                volume_usd_e8: observation.volume_usd_e8,
            },
        )
        .collect::<Vec<_>>();
    let discount_metrics = postfiat_execution::market_policy::compute_discount_metrics(
        &discount_observations,
        nav_floor.nav_floor_usd_e8,
        u128::from(envelope.discount_trigger_bps),
        u128::from(window_seconds),
    )
    .map_err(|error| format!("{error}"))?;
    let discount_response_bps = postfiat_execution::market_policy::compute_discount_response_bps(
        discount_metrics.response_curve_metrics(),
    )
    .map_err(|error| format!("{error}"))?;
    let reserve_cap = postfiat_execution::market_policy::compute_reserve_deploy_cap(
        envelope.funded_alignment_reserve_usd_e8,
        discount_response_bps,
        postfiat_execution::market_policy::ReserveDeployLimits {
            available_alignment_reserve_usd_e8: inputs
                .reserve_limits
                .available_alignment_reserve_usd_e8,
            venue_policy_cap_usd_e8: inputs.reserve_limits.venue_policy_cap_usd_e8,
            depth_limited_cap_usd_e8: inputs.reserve_limits.depth_limited_cap_usd_e8,
            cooldown_limited_cap_usd_e8: inputs.reserve_limits.cooldown_limited_cap_usd_e8,
        },
    )
    .map_err(|error| format!("{error}"))?;
    let premium_metrics = postfiat_execution::market_policy::compute_premium_metrics(
        &premium_observations,
        nav_floor.nav_floor_usd_e8,
        u128::from(envelope.premium_trigger_bps),
        u128::from(window_seconds),
    )
    .map_err(|error| format!("{error}"))?;
    let premium_response_bps = postfiat_execution::market_policy::compute_premium_response_bps(
        premium_metrics.response_curve_metrics(),
    )
    .map_err(|error| format!("{error}"))?;
    let mint_cap = postfiat_execution::market_policy::compute_mint_cap(
        valid_global_supply_atoms,
        premium_response_bps,
        backing_capacity.verified_capacity_remaining_atoms,
        postfiat_execution::market_policy::MintCapLimits {
            policy_max_mint_atoms: inputs.mint_limits.policy_max_mint_atoms,
            venue_bid_depth_atoms: inputs.mint_limits.venue_bid_depth_atoms,
            cooldown_mint_atoms: inputs.mint_limits.cooldown_mint_atoms,
        },
    )
    .map_err(|error| format!("{error}"))?;

    let mut expected = envelope.clone();
    expected.asset_id = market_ops_asset_id(asset_id)?;
    expected.epoch = packet.epoch;
    expected.reserve_packet_hash = market_ops_reserve_packet_hash(&packet.reserve_packet_hash)?;
    expected.supply_packet_hash =
        market_ops_supply_packet_hash(asset_id, packet.epoch, valid_global_supply_atoms)?;
    expected.evidence_root = evidence_root;
    expected.nav_floor_usd_e8 = nav_floor.nav_floor_usd_e8;
    expected.valid_global_supply_atoms = valid_global_supply_atoms;
    expected.verified_net_assets_usd_e8 = verified_net_assets_usd_e8;
    expected.required_alignment_reserve_usd_e8 = alignment.required_alignment_reserve_next_usd_e8;
    expected.max_reserve_deploy_usd_e8 = reserve_cap.reserve_deploy_cap_usd_e8;
    expected.max_mint_atoms = mint_cap.mint_cap_atoms;
    Ok(expected)
}

pub(super) fn market_ops_parse_hex_array<const N: usize>(
    field: &str,
    value: &str,
) -> io::Result<[u8; N]> {
    let trimmed = value.strip_prefix("0x").unwrap_or(value);
    if trimmed.len() != N * 2 {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            format!("{field} must be {} hex chars", N * 2),
        ));
    }
    let bytes = hex_to_bytes(trimmed).map_err(|error| {
        io::Error::new(
            io::ErrorKind::InvalidInput,
            format!("{field} is not valid hex: {error}"),
        )
    })?;
    let mut output = [0u8; N];
    output.copy_from_slice(&bytes);
    Ok(output)
}

pub(super) fn derive_market_ops_operation_nonce(
    asset_id: &str,
    epoch: u64,
    reserve_packet_hash: &str,
    policy: &MarketOpsPolicyRegistration,
    data_window_start: u64,
    data_window_end: u64,
    expires_at: u64,
) -> [u8; 32] {
    let seed = format!(
        "asset_id={asset_id}\nepoch={epoch}\nreserve_packet_hash={reserve_packet_hash}\npolicy_hash={}\ndata_window_start={data_window_start}\ndata_window_end={data_window_end}\nexpires_at={expires_at}\n",
        bytes_to_hex(&policy.policy_hash)
    );
    let digest = hash_bytes(
        "postfiat.market_ops.operation_bundle_nonce.v1",
        seed.as_bytes(),
    );
    let mut nonce = [0u8; 32];
    nonce.copy_from_slice(&digest[..32]);
    nonce
}

