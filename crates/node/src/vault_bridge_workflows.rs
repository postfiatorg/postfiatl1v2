fn vault_bridge_withdrawal_verifier_proof_digest(
    evm_chain_id: u64,
    verifier_address: &str,
    packet_digest: &[u8],
    pftl_withdrawal_hash_commitment: &[u8],
    pftl_finalized_height: u64,
) -> io::Result<String> {
    let verifier_address = vault_bridge_evm_address_bytes("verifier_address", verifier_address)?;
    let head_words = 6usize;
    let head_len = head_words.checked_mul(EVM_ABI_WORD_BYTES).ok_or_else(|| {
        io::Error::new(io::ErrorKind::InvalidData, "proof digest ABI head overflow")
    })?;
    let mut head = Vec::with_capacity(head_len);
    let mut tail = Vec::new();
    vault_bridge_append_abi_dynamic_bytes(
        &mut head,
        &mut tail,
        head_len,
        "postfiat.erc20_bridge.withdrawal_proof.v1".as_bytes(),
    )?;
    vault_bridge_append_abi_u256_u64(&mut head, evm_chain_id);
    vault_bridge_append_abi_address(&mut head, &verifier_address);
    vault_bridge_append_abi_bytes32(&mut head, packet_digest, "packet_digest")?;
    vault_bridge_append_abi_bytes32(
        &mut head,
        pftl_withdrawal_hash_commitment,
        "pftl_withdrawal_hash_commitment",
    )?;
    vault_bridge_append_abi_u256_u64(&mut head, pftl_finalized_height);
    let mut abi = head;
    abi.extend_from_slice(&tail);
    Ok(vault_bridge_0x_hex(&bytes_to_hex(&vault_bridge_keccak256(
        &abi,
    ))))
}

fn vault_bridge_vault_pending_withdrawal_id(
    packet: &VaultBridgeWithdrawalPacket,
    packet_digest: &[u8],
    pftl_withdrawal_hash_commitment: &[u8],
) -> io::Result<String> {
    let withdrawal_id =
        vault_bridge_hex_bytes_exact("withdrawal_packet.withdrawal_id", &packet.withdrawal_id, 48)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    let burn_tx_id =
        vault_bridge_hex_bytes_exact("withdrawal_packet.burn_tx_id", &packet.burn_tx_id, 48)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    let head_words = 5usize;
    let head_len = head_words.checked_mul(EVM_ABI_WORD_BYTES).ok_or_else(|| {
        io::Error::new(io::ErrorKind::InvalidData, "pending id ABI head overflow")
    })?;
    let mut head = Vec::with_capacity(head_len);
    let mut tail = Vec::new();
    vault_bridge_append_abi_dynamic_bytes(
        &mut head,
        &mut tail,
        head_len,
        "postfiat.erc20_bridge.withdrawal_pending.v1".as_bytes(),
    )?;
    vault_bridge_append_abi_dynamic_bytes(&mut head, &mut tail, head_len, &withdrawal_id)?;
    vault_bridge_append_abi_dynamic_bytes(&mut head, &mut tail, head_len, &burn_tx_id)?;
    vault_bridge_append_abi_bytes32(
        &mut head,
        pftl_withdrawal_hash_commitment,
        "pftl_withdrawal_hash_commitment",
    )?;
    vault_bridge_append_abi_bytes32(&mut head, packet_digest, "packet_digest")?;
    let mut abi = head;
    abi.extend_from_slice(&tail);
    Ok(vault_bridge_0x_hex(&bytes_to_hex(&vault_bridge_keccak256(
        &abi,
    ))))
}

fn vault_bridge_abi_encode_dynamic_string_bytes32_bytes32(
    domain: &str,
    first: &[u8],
    second: &[u8],
) -> io::Result<Vec<u8>> {
    let head_words = 3usize;
    let head_len = head_words
        .checked_mul(EVM_ABI_WORD_BYTES)
        .ok_or_else(|| io::Error::new(io::ErrorKind::InvalidData, "ABI head overflow"))?;
    let mut head = Vec::with_capacity(head_len);
    let mut tail = Vec::new();
    vault_bridge_append_abi_dynamic_bytes(&mut head, &mut tail, head_len, domain.as_bytes())?;
    vault_bridge_append_abi_bytes32(&mut head, first, "first")?;
    vault_bridge_append_abi_bytes32(&mut head, second, "second")?;
    head.extend_from_slice(&tail);
    Ok(head)
}

fn vault_bridge_abi_encode_dynamic_string_bytes32_bytes32_u64(
    domain: &str,
    first: &[u8],
    second: &[u8],
    value: u64,
) -> io::Result<Vec<u8>> {
    let head_words = 4usize;
    let head_len = head_words
        .checked_mul(EVM_ABI_WORD_BYTES)
        .ok_or_else(|| io::Error::new(io::ErrorKind::InvalidData, "ABI head overflow"))?;
    let mut head = Vec::with_capacity(head_len);
    let mut tail = Vec::new();
    vault_bridge_append_abi_dynamic_bytes(&mut head, &mut tail, head_len, domain.as_bytes())?;
    vault_bridge_append_abi_bytes32(&mut head, first, "first")?;
    vault_bridge_append_abi_bytes32(&mut head, second, "second")?;
    vault_bridge_append_abi_u256_u64(&mut head, value);
    head.extend_from_slice(&tail);
    Ok(head)
}

pub(super) fn vault_bridge_append_abi_u256_u64(out: &mut Vec<u8>, value: u64) {
    out.extend_from_slice(&[0_u8; 24]);
    out.extend_from_slice(&value.to_be_bytes());
}

fn vault_bridge_append_abi_u256_usize(out: &mut Vec<u8>, value: usize) -> io::Result<()> {
    let value = u64::try_from(value)
        .map_err(|_| io::Error::new(io::ErrorKind::InvalidData, "ABI usize does not fit u64"))?;
    vault_bridge_append_abi_u256_u64(out, value);
    Ok(())
}

pub(super) fn vault_bridge_append_abi_address(out: &mut Vec<u8>, value: &[u8; 20]) {
    out.extend_from_slice(&[0_u8; 12]);
    out.extend_from_slice(value);
}

fn vault_bridge_append_abi_bytes32(out: &mut Vec<u8>, value: &[u8], field: &str) -> io::Result<()> {
    if value.len() != EVM_ABI_WORD_BYTES {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!("{field} must be 32 bytes, got {}", value.len()),
        ));
    }
    out.extend_from_slice(value);
    Ok(())
}

pub(super) fn vault_bridge_append_abi_dynamic_bytes(
    head: &mut Vec<u8>,
    tail: &mut Vec<u8>,
    head_len: usize,
    value: &[u8],
) -> io::Result<()> {
    let offset = head_len
        .checked_add(tail.len())
        .ok_or_else(|| io::Error::new(io::ErrorKind::InvalidData, "ABI dynamic offset overflow"))?;
    vault_bridge_append_abi_u256_usize(head, offset)?;
    vault_bridge_append_abi_u256_usize(tail, value.len())?;
    tail.extend_from_slice(value);
    let padding = (EVM_ABI_WORD_BYTES - (value.len() % EVM_ABI_WORD_BYTES)) % EVM_ABI_WORD_BYTES;
    tail.extend(std::iter::repeat_n(0_u8, padding));
    Ok(())
}

pub(super) fn vault_bridge_evm_address_bytes(field: &str, value: &str) -> io::Result<[u8; 20]> {
    let bytes = vault_bridge_hex_bytes_exact(field, value, 20)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    if bytes.iter().all(|byte| *byte == 0) {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!("{field} must not be the zero EVM address"),
        ));
    }
    let mut out = [0_u8; 20];
    out.copy_from_slice(&bytes);
    Ok(out)
}

pub fn export_vault_bridge_reserve_replay_bundle(
    options: VaultBridgeReserveReplayBundleExportOptions,
) -> io::Result<VaultBridgeReserveReplayBundle> {
    let store = NodeStore::new(options.data_dir);
    let genesis = store.read_genesis()?;
    let ledger = store.read_ledger()?;
    let shielded = store.read_shielded()?;
    let asset_definition = ledger
        .asset_definition(&options.asset_id)
        .cloned()
        .ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::NotFound,
                format!("missing issued asset `{}`", options.asset_id),
            )
        })?;
    let nav_asset = ledger
        .nav_asset(&options.asset_id)
        .cloned()
        .ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::NotFound,
                format!("missing NAV asset `{}`", options.asset_id),
            )
        })?;
    let reserve_packet = ledger
        .nav_reserve_packets
        .iter()
        .find(|packet| {
            packet.asset_id == options.asset_id
                && packet.epoch == options.epoch
                && packet.state == NAV_RESERVE_STATE_FINALIZED
                && (nav_asset.finalized_epoch != options.epoch
                    || packet.reserve_packet_hash == nav_asset.finalized_reserve_packet_hash)
        })
        .cloned()
        .ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::NotFound,
                format!(
                    "missing finalized vault bridge asset reserve packet for asset `{}` epoch `{}`",
                    options.asset_id, options.epoch
                ),
            )
        })?;

    let mut trustlines = ledger
        .trustlines
        .iter()
        .filter(|line| line.asset_id == options.asset_id)
        .cloned()
        .collect::<Vec<_>>();
    trustlines.sort_by(|left, right| left.account.cmp(&right.account));
    let mut escrows = ledger
        .escrows
        .iter()
        .filter(|escrow| escrow.asset_id == options.asset_id && escrow.state == ESCROW_STATE_OPEN)
        .cloned()
        .collect::<Vec<_>>();
    escrows.sort_by(|left, right| left.escrow_id.cmp(&right.escrow_id));
    let mut offers = ledger
        .offers
        .iter()
        .filter(|offer| {
            offer.taker_gets_asset_id == options.asset_id && offer.state == OFFER_STATE_OPEN
        })
        .cloned()
        .collect::<Vec<_>>();
    offers.sort_by(|left, right| left.offer_id.cmp(&right.offer_id));
    let fast_asset_id = postfiat_types::FastAssetIdV1(
        hex_to_bytes(&options.asset_id)
            .map_err(invalid_data)?
            .try_into()
            .map_err(|_| {
                io::Error::new(
                    io::ErrorKind::InvalidData,
                    "vault bridge replay asset id does not fit FastLane asset width",
                )
            })?,
    );
    let mut fast_lane_reserves = ledger
        .fast_lane_reserves
        .iter()
        .filter(|reserve| reserve.asset_id == fast_asset_id)
        .cloned()
        .collect::<Vec<_>>();
    fast_lane_reserves.sort_by_key(|reserve| reserve.asset_id);
    let mut pftl_uniswap_routes = ledger
        .pftl_uniswap_routes
        .iter()
        .filter(|route| route.native_nav_asset_id == options.asset_id)
        .cloned()
        .collect::<Vec<_>>();
    pftl_uniswap_routes.sort_by(|left, right| left.route_id.cmp(&right.route_id));
    let mut asset_orchard_balances = shielded
        .orchard
        .as_ref()
        .map(|pool| {
            pool.asset_orchard_balances
                .iter()
                .filter(|balance| balance.asset_id == options.asset_id)
                .cloned()
                .collect::<Vec<_>>()
        })
        .unwrap_or_default();
    asset_orchard_balances.sort_by(|left, right| left.asset_id.cmp(&right.asset_id));
    let mut buckets = ledger
        .vault_bridge_bucket_states
        .iter()
        .filter(|bucket| bucket.asset_id == options.asset_id)
        .cloned()
        .collect::<Vec<_>>();
    buckets.sort_by(|left, right| left.bucket_id.cmp(&right.bucket_id));
    let mut receipts = ledger
        .vault_bridge_receipts
        .iter()
        .filter(|receipt| receipt.asset_id == options.asset_id)
        .cloned()
        .collect::<Vec<_>>();
    receipts.sort_by(|left, right| left.receipt_id.cmp(&right.receipt_id));
    let mut allocations = ledger
        .vault_bridge_allocations
        .iter()
        .filter(|allocation| allocation.asset_id == options.asset_id)
        .cloned()
        .collect::<Vec<_>>();
    allocations.sort_by(|left, right| left.allocation_id.cmp(&right.allocation_id));
    let mut redemptions = ledger
        .vault_bridge_redemptions
        .iter()
        .filter(|redemption| redemption.asset_id == options.asset_id)
        .cloned()
        .collect::<Vec<_>>();
    redemptions.sort_by(|left, right| left.redemption_id.cmp(&right.redemption_id));

    let expected_source_root = vault_bridge_source_root_for_asset(&buckets, &options.asset_id)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    let expected_counted_value_atoms =
        vault_bridge_counted_value_for_asset(&buckets, &options.asset_id)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    let expected_issued_supply_atoms =
        global_issued_asset_supply(&ledger, &shielded, &options.asset_id)?;

    let bundle = VaultBridgeReserveReplayBundle {
        schema: VAULT_BRIDGE_RESERVE_REPLAY_BUNDLE_SCHEMA.to_string(),
        chain_id: genesis.chain_id,
        protocol_version: genesis.protocol_version,
        asset_id: options.asset_id.clone(),
        epoch: options.epoch,
        asset_definition,
        nav_asset,
        reserve_packet,
        trustlines,
        escrows,
        offers,
        fast_lane_reserves,
        pftl_uniswap_routes,
        asset_orchard_balances,
        buckets,
        receipts,
        allocations,
        redemptions,
        expected_source_root,
        expected_counted_value_atoms,
        expected_issued_supply_atoms,
        expected_reserve_packet_hash: String::new(),
    };
    let mut bundle = bundle;
    bundle.expected_reserve_packet_hash = bundle.reserve_packet.reserve_packet_hash.clone();
    replay_vault_bridge_reserve_bundle_data(&bundle)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    std::fs::create_dir_all(&options.bundle_dir)?;
    let bundle_file = vault_bridge_reserve_replay_bundle_file(&options.bundle_dir);
    if bundle_file.exists() && !options.overwrite {
        return Err(io::Error::new(
            io::ErrorKind::AlreadyExists,
            format!(
                "vault bridge asset reserve replay bundle `{}` already exists; pass --overwrite to replace it",
                bundle_file.display()
            ),
        ));
    }
    let json = serde_json::to_string_pretty(&bundle).map_err(invalid_data)?;
    atomic_write(&bundle_file, format!("{json}\n"))?;
    Ok(bundle)
}

pub fn replay_vault_bridge_reserve_bundle(
    options: VaultBridgeReserveReplayBundleVerifyOptions,
) -> io::Result<VaultBridgeReserveReplayReport> {
    let bundle_file = vault_bridge_reserve_replay_bundle_file(&options.bundle_dir);
    let bundle: VaultBridgeReserveReplayBundle =
        read_json_file(&bundle_file, "vault bridge asset reserve replay bundle")?;
    let (source_root, counted_value_atoms, issued_supply_atoms) =
        replay_vault_bridge_reserve_bundle_data(&bundle)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    Ok(VaultBridgeReserveReplayReport {
        schema: VAULT_BRIDGE_RESERVE_REPLAY_REPORT_SCHEMA.to_string(),
        bundle_file: bundle_file.display().to_string(),
        asset_id: bundle.asset_id,
        epoch: bundle.epoch,
        expected_reserve_packet_hash: bundle.expected_reserve_packet_hash,
        source_root,
        counted_value_atoms,
        issued_supply_atoms,
        verified: true,
    })
}

pub(super) fn build_market_ops_public_status(
    ledger: &LedgerState,
    record: &FinalizedMarketOpsEnvelope,
    block_height: u64,
    now_unix: u64,
) -> Result<MarketOpsPublicStatus, String> {
    record.validate()?;
    let nav_asset = ledger.nav_asset(&record.asset_id);
    let source_packet = ledger.nav_reserve_packets.iter().find(|packet| {
        packet.asset_id == record.asset_id
            && packet.epoch == record.epoch
            && market_ops_reserve_packet_hash(&packet.reserve_packet_hash)
                .is_ok_and(|hash| hash == record.envelope.reserve_packet_hash)
    });
    let profile = nav_asset.and_then(|asset| ledger.nav_proof_profile(&asset.proof_profile));

    let reserve_packet_age_blocks = source_packet
        .and_then(|packet| block_height.checked_sub(packet.submitted_at_height))
        .unwrap_or_default();
    let supply_packet_age_blocks = nav_asset
        .and_then(|asset| block_height.checked_sub(asset.finalized_at_height))
        .unwrap_or_default();

    let reserve_packet_fresh = source_packet.is_some_and(|packet| {
        packet.state == NAV_RESERVE_STATE_FINALIZED
            && nav_asset.is_some_and(|asset| {
                asset.finalized_epoch == packet.epoch
                    && asset.finalized_reserve_packet_hash == packet.reserve_packet_hash
            })
            && profile.map_or(true, |profile| {
                profile.max_snapshot_age_blocks == 0
                    || packet.submitted_at_height == 0
                    || block_height
                        <= packet
                            .submitted_at_height
                            .saturating_add(profile.max_snapshot_age_blocks)
            })
    });
    let supply_packet_fresh = nav_asset.is_some_and(|asset| {
        asset.finalized_epoch == record.epoch
            && profile.map_or(true, |profile| {
                profile.max_epoch_gap_blocks == 0
                    || asset.finalized_at_height == 0
                    || block_height
                        <= asset
                            .finalized_at_height
                            .saturating_add(profile.max_epoch_gap_blocks)
            })
    });

    let status = if nav_asset.is_some_and(|asset| asset.halted) {
        MARKET_OPS_STATUS_PAUSED
    } else if source_packet.is_none() || nav_asset.is_none() {
        MARKET_OPS_STATUS_MISSING_SOURCE_PACKET
    } else if !reserve_packet_fresh || !supply_packet_fresh {
        MARKET_OPS_STATUS_STALE
    } else if now_unix > record.envelope.expires_at {
        MARKET_OPS_STATUS_EXPIRED
    } else if record.envelope.funded_alignment_reserve_usd_e8
        < record.envelope.required_alignment_reserve_usd_e8
    {
        MARKET_OPS_STATUS_UNDERFUNDED
    } else {
        MARKET_OPS_STATUS_ACTIVE
    };
    let caps_enabled = status == MARKET_OPS_STATUS_ACTIVE;

    let public_status = MarketOpsPublicStatus {
        schema: MARKET_OPS_PUBLIC_STATUS_SCHEMA.to_string(),
        asset_id: record.asset_id.clone(),
        nav_floor_usd_e8: record.envelope.nav_floor_usd_e8,
        verified_net_assets_usd_e8: record.envelope.verified_net_assets_usd_e8,
        valid_global_supply_atoms: record.envelope.valid_global_supply_atoms,
        reserve_packet_fresh,
        supply_packet_fresh,
        reserve_packet_age_blocks,
        supply_packet_age_blocks,
        funded_alignment_reserve_usd_e8: record.envelope.funded_alignment_reserve_usd_e8,
        required_alignment_reserve_usd_e8: record.envelope.required_alignment_reserve_usd_e8,
        current_reserve_deploy_cap_usd_e8: if caps_enabled {
            record.envelope.max_reserve_deploy_usd_e8
        } else {
            0
        },
        current_mint_cap_atoms: if caps_enabled {
            record.envelope.max_mint_atoms
        } else {
            0
        },
        market_operations_status: status.to_string(),
        accepted_policy_hash: bytes_to_hex(&record.envelope.policy_hash),
        envelope_hash: record.envelope_hash.clone(),
        envelope_epoch: record.epoch,
        packet_expires_at: record.envelope.expires_at,
        disclosure: MARKET_OPS_PUBLIC_DISCLOSURE.to_string(),
    };
    public_status.validate()?;
    Ok(public_status)
}

