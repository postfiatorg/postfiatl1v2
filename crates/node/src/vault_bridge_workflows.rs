fn vault_bridge_receipt_u64_field(
    receipt: &serde_json::Value,
    fields: &[&str],
    label: &str,
) -> io::Result<u64> {
    let value = fields
        .iter()
        .find_map(|field| receipt.get(*field))
        .ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::InvalidData,
                format!("source receipt missing {label}"),
            )
        })?;
    vault_bridge_json_u64(label, value)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))
}

fn vault_bridge_parse_u64_text(field: &str, value: &str) -> Result<u64, String> {
    if value.is_empty() {
        return Err(format!("{field} is empty"));
    }
    if let Some(hex) = value.strip_prefix("0x") {
        return u64::from_str_radix(hex, 16).map_err(|_| format!("{field} is not a u64 hex"));
    }
    value
        .parse::<u64>()
        .map_err(|_| format!("{field} is not a u64 integer"))
}

fn vault_bridge_validate_receipt_success(receipt: &serde_json::Value) -> io::Result<()> {
    let Some(status) = receipt.get("status") else {
        return Ok(());
    };
    let success = match status {
        serde_json::Value::Bool(value) => *value,
        serde_json::Value::Number(value) => value.as_u64() == Some(1),
        serde_json::Value::String(value) => {
            let normalized = value.trim().to_ascii_lowercase();
            matches!(normalized.as_str(), "0x1" | "1" | "true" | "success")
        }
        _ => false,
    };
    if !success {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            "source receipt status is not successful",
        ));
    }
    Ok(())
}

pub(super) fn vault_bridge_write_json_file<T: Serialize>(path: &Path, value: &T) -> io::Result<()> {
    let json = serde_json::to_string_pretty(value).map_err(invalid_data)?;
    atomic_write(path, format!("{json}\n"))
}

