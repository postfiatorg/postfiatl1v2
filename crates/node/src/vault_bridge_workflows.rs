fn vault_bridge_asset_operation_source(
    operation: &AssetTransactionOperation,
) -> Result<&str, String> {
    match operation {
        AssetTransactionOperation::AssetCreate(operation) => Ok(&operation.issuer),
        AssetTransactionOperation::TrustSet(operation) => Ok(&operation.account),
        AssetTransactionOperation::NavAssetRegister(operation) => Ok(&operation.issuer),
        AssetTransactionOperation::NavProfileRegister(operation) => Ok(&operation.registrant),
        AssetTransactionOperation::VaultBridgeDepositPropose(operation) => Ok(&operation.proposer),
        AssetTransactionOperation::VaultBridgeDepositAttest(operation) => Ok(&operation.attestor),
        AssetTransactionOperation::VaultBridgeDepositFinalize(operation) => {
            Ok(&operation.finalizer)
        }
        AssetTransactionOperation::VaultBridgeDepositClaim(operation) => Ok(&operation.claimer),
        AssetTransactionOperation::VaultBridgeBurnToRedeem(operation) => Ok(&operation.owner),
        _ => Err("vault bridge relay bundle does not support this asset operation".to_string()),
    }
}

