fn vault_bridge_withdrawal_signature_request(
    plan: &VaultBridgeWithdrawalPlanReport,
    evm_chain_id: u64,
    verifier_address: &str,
) -> io::Result<VaultBridgeWithdrawalSignatureRequest> {
    let digest_to_sign = plan.verifier_proof_digest_to_sign.clone().ok_or_else(|| {
        io::Error::new(io::ErrorKind::InvalidData, "missing verifier proof digest")
    })?;
    let cast_wallet_sign_command = format!(
        "cast wallet sign --no-hash {} --private-key \"$PFTL_WITHDRAWAL_SIGNER_PRIVATE_KEY\"",
        vault_bridge_shell_single_quote(&digest_to_sign)
    );
    Ok(VaultBridgeWithdrawalSignatureRequest {
        schema: VAULT_BRIDGE_WITHDRAWAL_SIGNATURE_REQUEST_SCHEMA.to_string(),
        asset_id: plan.asset_id.clone(),
        redemption_id: plan.redemption_id.clone(),
        pftl_finalized_height: plan.pftl_finalized_height,
        evm_chain_id,
        verifier_address: verifier_address.to_string(),
        withdrawal_packet_evm_digest: plan.withdrawal_packet_evm_digest.clone(),
        pftl_withdrawal_hash_commitment: plan.pftl_withdrawal_hash_commitment.clone(),
        verifier_proof_digest_to_sign: digest_to_sign,
        verifier_pending_proof_id: plan.verifier_pending_proof_id.clone(),
        verifier_withdrawal_key: plan.verifier_withdrawal_key.clone(),
        cast_wallet_sign_command,
        signatures_file_note:
            "Collect 65-byte 0x signatures over verifier_proof_digest_to_sign. signatures.json must be a JSON array sorted by recovered signer address ascending; the source-chain verifier enforces threshold, signer membership, low-s, and sorted uniqueness."
                .to_string(),
    })
}

