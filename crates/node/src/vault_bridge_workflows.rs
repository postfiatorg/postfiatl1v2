pub fn vault_bridge_asset_id(
    options: VaultBridgeAssetIdOptions,
) -> io::Result<VaultBridgeAssetIdReport> {
    let asset_id = issued_asset_id(
        &options.pftl_chain_id,
        &options.issuer,
        &options.asset_code,
        options.asset_version,
    )
    .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let evm_asset_id = vault_bridge_0x_hex(&asset_id);

    if let Some(path) = &options.env_file {
        if !options.overwrite && path.exists() {
            return Err(io::Error::new(
                io::ErrorKind::AlreadyExists,
                format!(
                    "vault bridge asset id env file `{}` already exists; pass --overwrite to replace it",
                    path.display()
                ),
            ));
        }
        if let Some(parent) = path.parent() {
            if !parent.as_os_str().is_empty() {
                std::fs::create_dir_all(parent)?;
            }
        }
        let contents = vault_bridge_asset_id_env_fragment(&options, &evm_asset_id)?;
        atomic_write(path, contents)?;
    }

    Ok(VaultBridgeAssetIdReport {
        schema: VAULT_BRIDGE_ASSET_ID_REPORT_SCHEMA.to_string(),
        pftl_chain_id: options.pftl_chain_id,
        issuer: options.issuer,
        asset_code: options.asset_code,
        asset_version: options.asset_version,
        asset_id,
        evm_asset_id,
        env_file: options.env_file.map(|path| path.display().to_string()),
        trust_boundary:
            "deterministic asset id predeploy helper only; bridge backing still requires explicit bootstrap operations and verified vault deposit evidence"
                .to_string(),
    })
}

fn vault_bridge_asset_id_env_fragment(
    options: &VaultBridgeAssetIdOptions,
    evm_asset_id: &str,
) -> io::Result<String> {
    Ok(format!(
        "# Generated by postfiat-node vault-bridge-asset-id.\n\
         # Review product parameters before deploying a source-chain vault.\n\
         PFTL_CHAIN_ID={}\n\
         PFTL_ISSUER={}\n\
         VAULT_BRIDGE_ASSET_CODE={}\n\
         VAULT_BRIDGE_ASSET_VERSION={}\n\
         VAULT_BRIDGE_ASSET_ID={}\n",
        vault_bridge_shell_env_value(&options.pftl_chain_id)?,
        vault_bridge_shell_env_value(&options.issuer)?,
        vault_bridge_shell_env_value(&options.asset_code)?,
        options.asset_version,
        vault_bridge_shell_env_value(evm_asset_id)?,
    ))
}

fn vault_bridge_shell_env_value(value: &str) -> io::Result<String> {
    if value.bytes().any(|byte| matches!(byte, b'\n' | b'\r' | 0)) {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "vault bridge env values must not contain newline or NUL bytes",
        ));
    }
    if value.bytes().all(|byte| {
        byte.is_ascii_alphanumeric() || matches!(byte, b'_' | b'-' | b'.' | b':' | b'/' | b'@')
    }) {
        return Ok(value.to_string());
    }
    Ok(format!("'{}'", value.replace('\'', "'\\''")))
}

pub fn vault_bridge_bootstrap_bundle(
    options: VaultBridgeBootstrapBundleOptions,
) -> io::Result<VaultBridgeBootstrapBundleReport> {
    if options.source_chain_id == 0 {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "source chain id must be nonzero",
        ));
    }
    if options.valuation_policy_hash.is_empty() {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "valuation policy hash must be nonempty",
        ));
    }
    if options.trust_limit == 0 && !options.trust_accounts.is_empty() {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "trust limit must be nonzero when trust accounts are provided",
        ));
    }

    let vault_address =
        vault_bridge_normalized_evm_address_text("--vault-address", &options.vault_address)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let token_address =
        vault_bridge_normalized_evm_address_text("--token-address", &options.token_address)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let source_domain = format!(
        "erc20_bridge_vault:{}:{}:{}",
        options.source_chain_id, vault_address, token_address
    );
    let source_class = format!("vault_bridge:{source_domain}");
    let asset_id = issued_asset_id(
        &options.pftl_chain_id,
        &options.issuer,
        &options.asset_code,
        options.asset_version,
    )
    .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let profile_id = nav_proof_profile_id_with_bridge_observer_min_confirmations(
        &options.verifier_kind,
        &source_class,
        options.max_snapshot_age_blocks,
        options.challenge_window_blocks,
        options.max_epoch_gap_blocks,
        options.settle_deadline_blocks,
        options.min_challenge_bond,
        options.min_attestations,
        options.tolerance_bp,
        options.bridge_observer_min_confirmations,
        &options.valuation_policy_hash,
        "",
        "",
        0,
        0,
    )
    .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let profile_register_operation =
        AssetTransactionOperation::NavProfileRegister(NavProfileRegisterOperation {
            registrant: options.issuer.clone(),
            verifier_kind: options.verifier_kind.clone(),
            source_class: source_class.clone(),
            max_snapshot_age_blocks: options.max_snapshot_age_blocks,
            challenge_window_blocks: options.challenge_window_blocks,
            max_epoch_gap_blocks: options.max_epoch_gap_blocks,
            settle_deadline_blocks: options.settle_deadline_blocks,
            min_challenge_bond: options.min_challenge_bond,
            min_attestations: options.min_attestations,
            tolerance_bp: options.tolerance_bp,
            bridge_observer_min_confirmations: options.bridge_observer_min_confirmations,
            valuation_policy_hash: options.valuation_policy_hash.clone(),
            vault_bridge_route_policy_hash: String::new(),
            sp1_program_vkey: String::new(),
            sp1_proof_encoding: String::new(),
            max_proof_bytes: 0,
            max_public_values_bytes: 0,
        });
    profile_register_operation
        .validate()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let asset_create_operation = AssetTransactionOperation::AssetCreate(AssetCreateOperation {
        issuer: options.issuer.clone(),
        code: options.asset_code.clone(),
        version: options.asset_version,
        precision: options.asset_precision,
        display_name: options.asset_display_name.clone(),
        max_supply: options.max_supply,
        requires_authorization: false,
        freeze_enabled: true,
        clawback_enabled: false,
    });
    asset_create_operation
        .validate()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let nav_asset_register_operation =
        AssetTransactionOperation::NavAssetRegister(NavAssetRegisterOperation {
            issuer: options.issuer.clone(),
            asset_id: asset_id.clone(),
            reserve_operator: options.reserve_operator.clone(),
            proof_profile: profile_id.clone(),
            valuation_unit: options.valuation_unit.clone(),
            redemption_account: options.redemption_account.clone(),
        });
    nav_asset_register_operation
        .validate()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let mut trust_set_operations = Vec::new();
    for account in &options.trust_accounts {
        let operation = AssetTransactionOperation::TrustSet(TrustSetOperation {
            account: account.clone(),
            issuer: options.issuer.clone(),
            asset_id: asset_id.clone(),
            limit: options.trust_limit,
            authorized: false,
            frozen: false,
            reserve_paid: options.trust_reserve_paid,
        });
        operation
            .validate()
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
        trust_set_operations.push(operation);
    }

    let bundle_dir = options.bundle_dir;
    let profile_register_operation_file = bundle_dir.join("profile-register.operation.json");
    let asset_create_operation_file = bundle_dir.join("asset-create.operation.json");
    let nav_asset_register_operation_file = bundle_dir.join("nav-asset-register.operation.json");
    let trust_set_operation_paths = trust_set_operations
        .iter()
        .enumerate()
        .map(|(index, _)| bundle_dir.join(format!("trust-set-{index}.operation.json")))
        .collect::<Vec<_>>();
    let commands_file = bundle_dir.join("commands.sh");

    let mut files = vec![
        profile_register_operation_file.clone(),
        asset_create_operation_file.clone(),
        nav_asset_register_operation_file.clone(),
        commands_file.clone(),
    ];
    files.extend(trust_set_operation_paths.iter().cloned());
    if !options.overwrite {
        if let Some(existing) = files.iter().find(|path| path.exists()) {
            return Err(io::Error::new(
                io::ErrorKind::AlreadyExists,
                format!(
                    "vault bridge bootstrap bundle file `{}` already exists; pass --overwrite to replace it",
                    existing.display()
                ),
            ));
        }
    }

    std::fs::create_dir_all(&bundle_dir)?;
    vault_bridge_write_json_file(
        &profile_register_operation_file,
        &profile_register_operation,
    )?;
    vault_bridge_write_json_file(&asset_create_operation_file, &asset_create_operation)?;
    vault_bridge_write_json_file(
        &nav_asset_register_operation_file,
        &nav_asset_register_operation,
    )?;
    for (path, operation) in trust_set_operation_paths
        .iter()
        .zip(trust_set_operations.iter())
    {
        vault_bridge_write_json_file(path, operation)?;
    }

    let mut commands = Vec::new();
    commands.extend(vault_bridge_operation_relay_commands(
        "profile-register",
        &profile_register_operation,
        "ISSUER_KEY_FILE",
    )?);
    commands.extend(vault_bridge_operation_relay_commands(
        "asset-create",
        &asset_create_operation,
        "ISSUER_KEY_FILE",
    )?);
    commands.extend(vault_bridge_operation_relay_commands(
        "nav-asset-register",
        &nav_asset_register_operation,
        "ISSUER_KEY_FILE",
    )?);
    for (index, operation) in trust_set_operations.iter().enumerate() {
        commands.extend(vault_bridge_operation_relay_commands(
            &format!("trust-set-{index}"),
            operation,
            &format!("TRUST_ACCOUNT_{index}_KEY_FILE"),
        )?);
    }
    let commands_script = vault_bridge_bootstrap_commands_script(
        &bundle_dir,
        !trust_set_operations.is_empty(),
        trust_set_operations.len(),
        &commands,
    );
    atomic_write(&commands_file, commands_script)?;

    Ok(VaultBridgeBootstrapBundleReport {
        schema: VAULT_BRIDGE_BOOTSTRAP_BUNDLE_SCHEMA.to_string(),
        bundle_dir: bundle_dir.display().to_string(),
        pftl_chain_id: options.pftl_chain_id,
        source_chain_id: options.source_chain_id,
        vault_address,
        token_address,
        source_domain,
        source_class,
        asset_id,
        profile_id,
        issuer: options.issuer,
        reserve_operator: options.reserve_operator,
        redemption_account: options.redemption_account,
        asset_code: options.asset_code,
        asset_version: options.asset_version,
        asset_precision: options.asset_precision,
        valuation_unit: options.valuation_unit,
        profile_register_operation_file: profile_register_operation_file.display().to_string(),
        asset_create_operation_file: asset_create_operation_file.display().to_string(),
        nav_asset_register_operation_file: nav_asset_register_operation_file.display().to_string(),
        trust_set_operation_files: trust_set_operation_paths
            .iter()
            .map(|path| path.display().to_string())
            .collect(),
        commands_file: commands_file.display().to_string(),
        commands,
        profile_register_operation,
        asset_create_operation,
        nav_asset_register_operation,
        trust_set_operations,
        trust_boundary:
            "PFTL bootstrap operations only; source-chain funds are controlled solely by the deployed ERC20BridgeVault and PFTL minting still requires verified vault deposit evidence"
                .to_string(),
    })
}

pub fn vault_bridge_deposit_intent(
    options: VaultBridgeDepositIntentOptions,
) -> io::Result<VaultBridgeDepositIntentReport> {
    if options.source_chain_id == 0 {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "source chain id must be nonzero",
        ));
    }
    if options.amount_atoms == 0 {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "amount atoms must be nonzero",
        ));
    }
    let vault_address =
        vault_bridge_normalized_evm_address_text("--vault-address", &options.vault_address)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let token_address =
        vault_bridge_normalized_evm_address_text("--token-address", &options.token_address)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let depositor = vault_bridge_normalized_evm_address_text("--depositor", &options.depositor)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let nonce = vault_bridge_normalized_hex_text("--nonce", &options.nonce, EVM_ABI_WORD_BYTES)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let pftl_recipient_hash = vault_bridge_pftl_recipient_hash(&options.pftl_recipient)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let route_binding = vault_bridge_route_binding(&options.policy_hash, options.route_epoch)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let mut evidence = VaultBridgeDepositEvidence {
        source_chain_id: options.source_chain_id,
        vault_address: vault_address.clone(),
        token_address: token_address.clone(),
        depositor: depositor.clone(),
        pftl_recipient: options.pftl_recipient.clone(),
        pftl_recipient_hash,
        amount_atoms: options.amount_atoms,
        nonce,
        route_binding,
        deposit_id: String::new(),
        block_hash: "00".repeat(EVM_ABI_WORD_BYTES),
        tx_hash: "00".repeat(EVM_ABI_WORD_BYTES),
        log_index: 0,
    };
    evidence.deposit_id = vault_bridge_deposit_id(&evidence)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let approve_signature = "approve(address,uint256)".to_string();
    let approve_cast_args = vec![vault_address.clone(), options.amount_atoms.to_string()];
    let approve_cast_command = format!(
        "cast send --rpc-url \"$SOURCE_CHAIN_RPC_URL\" --from {depositor} {token_address} \"{approve_signature}\" {}",
        approve_cast_args.join(" ")
    );

    let nonce_arg = vault_bridge_0x_hex(&evidence.nonce);
    let deposit_signature = "depositV2(uint256,string,bytes32,bytes32)".to_string();
    let deposit_cast_args = vec![
        options.amount_atoms.to_string(),
        options.pftl_recipient.clone(),
        nonce_arg,
        vault_bridge_0x_hex(&evidence.route_binding),
    ];
    let deposit_cast_command = format!(
        "cast send --rpc-url \"$SOURCE_CHAIN_RPC_URL\" --from {depositor} {vault_address} \"{deposit_signature}\" {} {} {} {}",
        options.amount_atoms,
        vault_bridge_shell_single_quote(&options.pftl_recipient),
        vault_bridge_0x_hex(&evidence.nonce),
        vault_bridge_0x_hex(&evidence.route_binding),
    );
    let receipt_cast_command =
        "cast receipt --rpc-url \"$SOURCE_CHAIN_RPC_URL\" --json <deposit_tx_hash> > deposit-receipt.json"
            .to_string();

    let asset_id = options.asset_id;
    let policy_hash = options.policy_hash;
    let proposer = options
        .proposer
        .unwrap_or_else(|| "<pftl_proposer>".to_string());
    let expires_at_height = options
        .expires_at_height
        .map(|value| value.to_string())
        .unwrap_or_else(|| "<expires_at_height>".to_string());
    let bundle_dir = options
        .bundle_dir
        .map(|path| path.display().to_string())
        .unwrap_or_else(|| "deposit-relay-bundle".to_string());
    let relay_bundle_command = format!(
        "postfiat-node vault-bridge-deposit-relay-bundle --receipt-file deposit-receipt.json --vault-address {vault_address} --token-address {token_address} --asset-id {asset_id} --policy-hash {policy_hash} --proposer {proposer} --expires-at-height {expires_at_height} --bundle {}",
        vault_bridge_shell_single_quote(&bundle_dir)
    );
    let relay_rpc_bundle_command = format!(
        "postfiat-node vault-bridge-deposit-relay-rpc-bundle --source-rpc-url \"$SOURCE_CHAIN_RPC_URL\" --tx-hash <deposit_tx_hash> --vault-address {vault_address} --token-address {token_address} --asset-id {asset_id} --policy-hash {policy_hash} --proposer {proposer} --expires-at-height {expires_at_height} --bundle {}",
        vault_bridge_shell_single_quote(&bundle_dir)
    );

    Ok(VaultBridgeDepositIntentReport {
        schema: VAULT_BRIDGE_DEPOSIT_INTENT_SCHEMA.to_string(),
        event_signature_topic: format!("0x{VAULT_BRIDGE_VAULT_DEPOSIT_V2_EVENT_TOPIC}"),
        source_chain_id: evidence.source_chain_id,
        vault_address,
        token_address,
        depositor,
        amount_atoms: evidence.amount_atoms,
        pftl_recipient: evidence.pftl_recipient.clone(),
        pftl_recipient_hash: evidence.pftl_recipient_hash.clone(),
        nonce: vault_bridge_0x_hex(&evidence.nonce),
        route_binding: vault_bridge_0x_hex(&evidence.route_binding),
        route_epoch: options.route_epoch,
        expected_deposit_id: vault_bridge_0x_hex(&evidence.deposit_id),
        source_domain: evidence.source_domain(),
        source_asset: evidence.source_asset_ref(),
        source_tx_or_attestation: evidence.source_tx_or_attestation(),
        approve_signature,
        approve_cast_args,
        approve_cast_command,
        deposit_signature,
        deposit_cast_args,
        deposit_cast_command,
        receipt_cast_command,
        relay_bundle_command,
        relay_rpc_bundle_command,
        trust_boundary:
            "source-chain intent bound to an exact governed route profile; PFTL minting still requires the finalized ERC20BridgeDepositedV2 receipt evidence to match this deposit commitment"
                .to_string(),
    })
}

pub fn vault_bridge_deposit_plan(
    options: VaultBridgeDepositPlanOptions,
) -> io::Result<VaultBridgeDepositPlanReport> {
    let log = vault_bridge_deposit_plan_log_input(&options)?;
    let evidence = parse_vault_bridge_vault_deposit_log(&log)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    validate_vault_bridge_deposit_filters(
        &evidence,
        options.vault_address.as_deref(),
        options.token_address.as_deref(),
    )
    .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let evidence_root = vault_bridge_deposit_evidence_root(&evidence)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    let computed_source_public_values_hash =
        vault_bridge_deposit_public_values_hash(&evidence, &evidence_root, &options.policy_hash)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;

    let source_proof_kind = options.source_proof_kind.unwrap_or_default();
    let source_proof_hash = options.source_proof_hash.unwrap_or_default();
    let provided_source_public_values_hash = options.source_public_values_hash.unwrap_or_default();
    let operation_source_public_values_hash = if source_proof_kind.is_empty() {
        if !source_proof_hash.is_empty() || !provided_source_public_values_hash.is_empty() {
            return Err(io::Error::new(
                io::ErrorKind::InvalidInput,
                "source proof hashes require --source-proof-kind",
            ));
        }
        String::new()
    } else {
        if source_proof_kind != NAV_PROFILE_VERIFIER_SP1_GROTH16 {
            return Err(io::Error::new(
                io::ErrorKind::InvalidInput,
                format!("--source-proof-kind must be {NAV_PROFILE_VERIFIER_SP1_GROTH16}"),
            ));
        }
        if provided_source_public_values_hash.is_empty() {
            computed_source_public_values_hash.clone()
        } else if provided_source_public_values_hash != computed_source_public_values_hash {
            return Err(io::Error::new(
                io::ErrorKind::InvalidInput,
                "--source-public-values-hash does not match the vault log evidence",
            ));
        } else {
            provided_source_public_values_hash
        }
    };

    let propose_operation =
        AssetTransactionOperation::VaultBridgeDepositPropose(VaultBridgeDepositProposeOperation {
            proposer: options.proposer,
            asset_id: options.asset_id.clone(),
            evidence_root: evidence_root.clone(),
            evidence: evidence.clone(),
            policy_hash: options.policy_hash.clone(),
            source_proof_kind,
            source_proof_hash,
            source_public_values_hash: operation_source_public_values_hash,
            expires_at_height: options.expires_at_height,
        });
    propose_operation
        .validate()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let deposit_observation = options
        .observer_confirmation_depth
        .map(|depth| VaultBridgeDepositObservation::success_for_evidence(&evidence, depth));
    let deposit_observation_root = deposit_observation
        .as_ref()
        .map(vault_bridge_deposit_observation_root)
        .transpose()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;

    let attest_operation = options.attestor.map(|attestor| {
        let observation_root = deposit_observation_root
            .clone()
            .unwrap_or_else(|| evidence_root.clone());
        AssetTransactionOperation::VaultBridgeDepositAttest(VaultBridgeDepositAttestOperation {
            attestor,
            asset_id: options.asset_id.clone(),
            evidence_root: evidence_root.clone(),
            pass: true,
            observation_root,
            observation: deposit_observation.clone(),
        })
    });
    if let Some(operation) = &attest_operation {
        operation
            .validate()
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    }

    let finalize_operation = AssetTransactionOperation::VaultBridgeDepositFinalize(
        VaultBridgeDepositFinalizeOperation {
            finalizer: options.finalizer,
            asset_id: options.asset_id.clone(),
            evidence_root: evidence_root.clone(),
        },
    );
    finalize_operation
        .validate()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let claim_operation =
        AssetTransactionOperation::VaultBridgeDepositClaim(VaultBridgeDepositClaimOperation {
            claimer: options.claimer,
            asset_id: options.asset_id.clone(),
            evidence_root: evidence_root.clone(),
            policy_hash: options.policy_hash.clone(),
            recipient: evidence.pftl_recipient.clone(),
            amount_atoms: evidence.amount_atoms,
        });
    claim_operation
        .validate()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    Ok(VaultBridgeDepositPlanReport {
        schema: VAULT_BRIDGE_DEPOSIT_PLAN_SCHEMA.to_string(),
        event_signature_topic: format!(
            "0x{}",
            if evidence.route_binding.is_empty() {
                VAULT_BRIDGE_VAULT_DEPOSIT_EVENT_TOPIC
            } else {
                VAULT_BRIDGE_VAULT_DEPOSIT_V2_EVENT_TOPIC
            }
        ),
        asset_id: options.asset_id,
        policy_hash: options.policy_hash,
        evidence_root,
        source_public_values_hash: computed_source_public_values_hash,
        deposit_observation_root,
        deposit_confirmation_depth: options.observer_confirmation_depth,
        source_domain: evidence.source_domain(),
        source_tx_or_attestation: evidence.source_tx_or_attestation(),
        finality_ref: evidence.finality_ref(),
        vault_id: evidence.vault_id(),
        evidence,
        propose_operation,
        attest_operation,
        finalize_operation,
        claim_operation,
        trust_boundary:
            "validated ERC20BridgeVault event payload; Ethereum receipt inclusion still requires the configured source proof or challenge/finality path"
                .to_string(),
    })
}

pub fn vault_bridge_withdrawal_plan(
    options: VaultBridgeWithdrawalPlanOptions,
) -> io::Result<VaultBridgeWithdrawalPlanReport> {
    let store = NodeStore::new(options.data_dir);
    let genesis = store.read_genesis()?;
    let ledger = store.read_ledger()?;
    let redemption = ledger
        .vault_bridge_redemptions
        .iter()
        .find(|redemption| {
            redemption.asset_id == options.asset_id
                && redemption.redemption_id == options.redemption_id
        })
        .cloned()
        .ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::NotFound,
                format!(
                    "missing vault bridge redemption `{}` for asset `{}`",
                    options.redemption_id, options.asset_id
                ),
            )
        })?;
    redemption
        .validate_for_chain(&genesis.chain_id)
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    if redemption.state != VAULT_BRIDGE_REDEMPTION_STATE_PENDING {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            format!(
                "vault bridge redemption `{}` is `{}`, expected `{}`",
                redemption.redemption_id, redemption.state, VAULT_BRIDGE_REDEMPTION_STATE_PENDING
            ),
        ));
    }

    let pftl_finalized_height = options
        .pftl_finalized_height
        .unwrap_or(redemption.withdrawal_packet.finalized_height);
    if pftl_finalized_height == 0 {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "pftl finalized height must be nonzero",
        ));
    }

    let packet_digest = vault_bridge_hex_bytes_exact(
        "withdrawal_packet_evm_digest",
        &redemption.withdrawal_packet_evm_digest,
        32,
    )
    .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    let pftl_withdrawal_hash = vault_bridge_hex_bytes_exact(
        "withdrawal_packet_hash",
        &redemption.withdrawal_packet_hash,
        48,
    )
    .map_err(|error| io::Error::new(io::ErrorKind::InvalidData, error))?;
    let pftl_withdrawal_hash_commitment_bytes = vault_bridge_keccak256(&pftl_withdrawal_hash);
    let verifier_pending_proof_id = vault_bridge_withdrawal_verifier_pending_proof_id(
        &packet_digest,
        &pftl_withdrawal_hash_commitment_bytes,
        pftl_finalized_height,
    )?;
    let verifier_withdrawal_key = vault_bridge_withdrawal_verifier_key(
        &packet_digest,
        &pftl_withdrawal_hash_commitment_bytes,
    )?;
    let vault_pending_withdrawal_id = vault_bridge_vault_pending_withdrawal_id(
        &redemption.withdrawal_packet,
        &packet_digest,
        &pftl_withdrawal_hash_commitment_bytes,
    )?;

    let verifier_proof_digest_to_sign =
        match (options.evm_chain_id, options.verifier_address.as_deref()) {
            (Some(evm_chain_id), Some(verifier_address)) => {
                Some(vault_bridge_withdrawal_verifier_proof_digest(
                    evm_chain_id,
                    verifier_address,
                    &packet_digest,
                    &pftl_withdrawal_hash_commitment_bytes,
                    pftl_finalized_height,
                )?)
            }
            (None, None) => None,
            _ => {
                return Err(io::Error::new(
                    io::ErrorKind::InvalidInput,
                    "--evm-chain-id and --verifier-address must be provided together",
                ))
            }
        };

    let signatures = match options.signatures_file.as_ref() {
        Some(path) => vault_bridge_withdrawal_signatures(path)?,
        None => Vec::new(),
    };
    let signatures_arg = vault_bridge_evm_bytes_array_arg(&signatures);
    let withdrawal_packet_tuple_arg =
        vault_bridge_withdrawal_packet_tuple_arg(&redemption.withdrawal_packet);
    let pftl_withdrawal_hash_hex = vault_bridge_0x_hex(&redemption.withdrawal_packet_hash);
    let packet_digest_hex = vault_bridge_0x_hex(&redemption.withdrawal_packet_evm_digest);
    let pftl_withdrawal_hash_commitment =
        vault_bridge_0x_hex(&bytes_to_hex(&pftl_withdrawal_hash_commitment_bytes));

    let verifier_submit_proof_signature = "submitProof(bytes32,bytes32,uint64,bytes[])".to_string();
    let verifier_submit_proof_cast_args = vec![
        packet_digest_hex.clone(),
        pftl_withdrawal_hash_commitment.clone(),
        pftl_finalized_height.to_string(),
        signatures_arg,
    ];
    let verifier_submit_proof_cast_command = format!(
        "cast send \"$PFTL_WITHDRAWAL_VERIFIER\" \"{}\" {}",
        verifier_submit_proof_signature,
        verifier_submit_proof_cast_args.join(" ")
    );

    let vault_submit_withdrawal_signature =
        "submitWithdrawal((uint64,uint256,address,address,bytes,bytes,bytes,address,uint256,bytes,bytes,uint64,bytes),bytes)"
            .to_string();
    let vault_submit_withdrawal_cast_args = vec![
        withdrawal_packet_tuple_arg.clone(),
        pftl_withdrawal_hash_hex.clone(),
    ];
    let vault_submit_withdrawal_cast_command = format!(
        "cast send \"$ERC20_BRIDGE_VAULT\" \"{}\" {}",
        vault_submit_withdrawal_signature,
        vault_submit_withdrawal_cast_args.join(" ")
    );

    Ok(VaultBridgeWithdrawalPlanReport {
        schema: VAULT_BRIDGE_WITHDRAWAL_PLAN_SCHEMA.to_string(),
        asset_id: redemption.asset_id.clone(),
        redemption_id: redemption.redemption_id.clone(),
        redemption_state: redemption.state.clone(),
        pftl_finalized_height,
        withdrawal_packet_evm_args: vault_bridge_withdrawal_packet_evm_args(
            &redemption.withdrawal_packet,
        ),
        withdrawal_packet: redemption.withdrawal_packet.clone(),
        withdrawal_packet_tuple_arg,
        withdrawal_packet_hash: redemption.withdrawal_packet_hash.clone(),
        pftl_withdrawal_hash: pftl_withdrawal_hash_hex,
        pftl_withdrawal_hash_commitment,
        withdrawal_packet_evm_digest: packet_digest_hex,
        verifier_pending_proof_id,
        verifier_withdrawal_key,
        verifier_proof_digest_to_sign,
        verifier_submit_proof_signature,
        verifier_submit_proof_cast_args,
        verifier_submit_proof_cast_command,
        vault_pending_withdrawal_id,
        vault_submit_withdrawal_signature,
        vault_submit_withdrawal_cast_args,
        vault_submit_withdrawal_cast_command,
        signatures,
        trust_boundary:
            "plan is derived from a finalized PFTL vault bridge burn; verifier signatures still must come from the configured PFTL finality signer set before the source-chain vault can release funds"
                .to_string(),
    })
}

pub fn vault_bridge_withdrawal_relay_bundle(
    options: VaultBridgeWithdrawalRelayBundleOptions,
) -> io::Result<VaultBridgeWithdrawalRelayBundleReport> {
    let bundle_dir = options.bundle_dir;
    let overwrite = options.overwrite;
    let plan = vault_bridge_withdrawal_plan(options.plan_options)?;

    let plan_file = bundle_dir.join("plan.json");
    let commands_file = bundle_dir.join("commands.sh");
    let files = [plan_file.clone(), commands_file.clone()];
    if !overwrite {
        if let Some(existing) = files.iter().find(|path| path.exists()) {
            return Err(io::Error::new(
                io::ErrorKind::AlreadyExists,
                format!(
                    "vault bridge withdrawal relay bundle file `{}` already exists; pass --overwrite to replace it",
                    existing.display()
                ),
            ));
        }
    }

    std::fs::create_dir_all(&bundle_dir)?;
    vault_bridge_write_json_file(&plan_file, &plan)?;

    let verifier_submit_proof_command = format!(
        "cast send --rpc-url \"$SOURCE_CHAIN_RPC_URL\" \"$PFTL_WITHDRAWAL_VERIFIER\" \"{}\" {}",
        plan.verifier_submit_proof_signature,
        vault_bridge_shell_words(&plan.verifier_submit_proof_cast_args)
    );
    let verifier_finalize_proof_command = format!(
        "cast send --rpc-url \"$SOURCE_CHAIN_RPC_URL\" \"$PFTL_WITHDRAWAL_VERIFIER\" \"finalizeProof(bytes32)\" {}",
        vault_bridge_shell_single_quote(&plan.verifier_pending_proof_id)
    );
    let vault_submit_withdrawal_command = format!(
        "cast send --rpc-url \"$SOURCE_CHAIN_RPC_URL\" \"$ERC20_BRIDGE_VAULT\" \"{}\" {}",
        plan.vault_submit_withdrawal_signature,
        vault_bridge_shell_words(&plan.vault_submit_withdrawal_cast_args)
    );
    let vault_finalize_withdrawal_command = format!(
        "cast send --rpc-url \"$SOURCE_CHAIN_RPC_URL\" \"$ERC20_BRIDGE_VAULT\" \"finalizeWithdrawal(bytes32)\" {}",
        vault_bridge_shell_single_quote(&plan.vault_pending_withdrawal_id)
    );
    let vault_claim_withdrawal_command = format!(
        "cast send --rpc-url \"$SOURCE_CHAIN_RPC_URL\" \"$ERC20_BRIDGE_VAULT\" \"claimWithdrawal(bytes32)\" {}",
        vault_bridge_shell_single_quote(&plan.vault_pending_withdrawal_id)
    );
    let stages = vec![
        "submit-proof".to_string(),
        "finalize-proof".to_string(),
        "submit-withdrawal".to_string(),
        "finalize-withdrawal".to_string(),
        "claim".to_string(),
    ];

    let commands_script = vault_bridge_withdrawal_relay_commands_script(
        &bundle_dir,
        &verifier_submit_proof_command,
        &verifier_finalize_proof_command,
        &vault_submit_withdrawal_command,
        &vault_finalize_withdrawal_command,
        &vault_claim_withdrawal_command,
    );
    atomic_write(&commands_file, commands_script)?;

    Ok(VaultBridgeWithdrawalRelayBundleReport {
        schema: VAULT_BRIDGE_WITHDRAWAL_RELAY_BUNDLE_SCHEMA.to_string(),
        bundle_dir: bundle_dir.display().to_string(),
        plan_file: plan_file.display().to_string(),
        commands_file: commands_file.display().to_string(),
        verifier_submit_proof_command,
        verifier_finalize_proof_command,
        vault_submit_withdrawal_command,
        vault_finalize_withdrawal_command,
        vault_claim_withdrawal_command,
        stages,
        plan,
        trust_boundary:
            "stage-based source-chain withdrawal relay; proof and vault challenge windows must elapse unchallenged before finalization and direct user claim"
                .to_string(),
    })
}

pub fn vault_bridge_withdrawal_signature_bundle(
    options: VaultBridgeWithdrawalSignatureBundleOptions,
) -> io::Result<VaultBridgeWithdrawalSignatureBundleReport> {
    if options.plan_options.evm_chain_id.is_none()
        || options.plan_options.verifier_address.is_none()
    {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "withdrawal signature bundle requires --evm-chain-id and --verifier-address",
        ));
    }
    if options.plan_options.signatures_file.is_some() {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "withdrawal signature bundle creates the signatures file; do not pass --signatures-file",
        ));
    }
    let evm_chain_id = options.plan_options.evm_chain_id.ok_or_else(|| {
        io::Error::new(
            io::ErrorKind::InvalidInput,
            "withdrawal signature bundle requires --evm-chain-id",
        )
    })?;
    let verifier_address = options
        .plan_options
        .verifier_address
        .clone()
        .ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::InvalidInput,
                "withdrawal signature bundle requires --verifier-address",
            )
        })?;
    let verifier_address =
        vault_bridge_normalized_evm_address_text("--verifier-address", &verifier_address)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let data_dir = options.plan_options.data_dir.clone();

    let bundle_dir = options.bundle_dir;
    let relay_bundle_dir = options
        .relay_bundle_dir
        .unwrap_or_else(|| bundle_dir.join("relay-bundle"));
    let plan = vault_bridge_withdrawal_plan(options.plan_options)?;
    let digest_to_sign = plan.verifier_proof_digest_to_sign.clone().ok_or_else(|| {
        io::Error::new(io::ErrorKind::InvalidData, "missing verifier proof digest")
    })?;

    let plan_file = bundle_dir.join("plan.json");
    let signature_request_file = bundle_dir.join("signature-request.json");
    let signatures_file = bundle_dir.join("signatures.json");
    let commands_file = bundle_dir.join("commands.sh");
    let files = [
        plan_file.clone(),
        signature_request_file.clone(),
        signatures_file.clone(),
        commands_file.clone(),
    ];
    if !options.overwrite {
        if let Some(existing) = files.iter().find(|path| path.exists()) {
            return Err(io::Error::new(
                io::ErrorKind::AlreadyExists,
                format!(
                    "vault bridge withdrawal signature bundle file `{}` already exists; pass --overwrite to replace it",
                    existing.display()
                ),
            ));
        }
    }

    let request =
        vault_bridge_withdrawal_signature_request(&plan, evm_chain_id, &verifier_address)?;
    let sign_command = request.cast_wallet_sign_command.clone();
    let relay_bundle_command = vault_bridge_withdrawal_signature_relay_bundle_command(
        &data_dir,
        &plan,
        evm_chain_id,
        &verifier_address,
        &signatures_file,
        &relay_bundle_dir,
    );
    let commands_script = vault_bridge_withdrawal_signature_commands_script(
        &bundle_dir,
        &digest_to_sign,
        &signatures_file,
        &relay_bundle_dir,
        &sign_command,
        &relay_bundle_command,
    );

    std::fs::create_dir_all(&bundle_dir)?;
    vault_bridge_write_json_file(&plan_file, &plan)?;
    vault_bridge_write_json_file(&signature_request_file, &request)?;
    vault_bridge_write_json_file(&signatures_file, &Vec::<String>::new())?;
    atomic_write(&commands_file, commands_script)?;

    Ok(VaultBridgeWithdrawalSignatureBundleReport {
        schema: VAULT_BRIDGE_WITHDRAWAL_SIGNATURE_BUNDLE_SCHEMA.to_string(),
        bundle_dir: bundle_dir.display().to_string(),
        plan_file: plan_file.display().to_string(),
        signature_request_file: signature_request_file.display().to_string(),
        signatures_file: signatures_file.display().to_string(),
        commands_file: commands_file.display().to_string(),
        relay_bundle_dir: relay_bundle_dir.display().to_string(),
        sign_command,
        relay_bundle_command,
        plan,
        signature_request: request,
        trust_boundary:
            "signature bundle contains only withdrawal proof digest material and signer instructions; source-chain release still requires threshold signatures, verifier challenge finality, vault challenge finality, and direct claim"
                .to_string(),
    })
}

pub fn vault_bridge_deposit_relay_bundle(
    options: VaultBridgeDepositRelayBundleOptions,
) -> io::Result<VaultBridgeDepositRelayBundleReport> {
    let bundle_dir = options.bundle_dir;
    let overwrite = options.overwrite;
    let plan = vault_bridge_deposit_plan(options.plan_options)?;

    let plan_file = bundle_dir.join("plan.json");
    let propose_operation_file = bundle_dir.join("propose.operation.json");
    let attest_operation_file = plan
        .attest_operation
        .as_ref()
        .map(|_| bundle_dir.join("attest.operation.json"));
    let finalize_operation_file = bundle_dir.join("finalize.operation.json");
    let claim_operation_file = bundle_dir.join("claim.operation.json");
    let commands_file = bundle_dir.join("commands.sh");

    let mut files = vec![
        plan_file.clone(),
        propose_operation_file.clone(),
        finalize_operation_file.clone(),
        claim_operation_file.clone(),
        commands_file.clone(),
    ];
    if let Some(path) = &attest_operation_file {
        files.push(path.clone());
    }
    if !overwrite {
        if let Some(existing) = files.iter().find(|path| path.exists()) {
            return Err(io::Error::new(
                io::ErrorKind::AlreadyExists,
                format!(
                    "vault bridge deposit relay bundle file `{}` already exists; pass --overwrite to replace it",
                    existing.display()
                ),
            ));
        }
    }

    std::fs::create_dir_all(&bundle_dir)?;
    vault_bridge_write_json_file(&plan_file, &plan)?;
    vault_bridge_write_json_file(&propose_operation_file, &plan.propose_operation)?;
    if let (Some(path), Some(operation)) = (&attest_operation_file, &plan.attest_operation) {
        vault_bridge_write_json_file(path, operation)?;
    }
    vault_bridge_write_json_file(&finalize_operation_file, &plan.finalize_operation)?;
    vault_bridge_write_json_file(&claim_operation_file, &plan.claim_operation)?;

    let mut commands = Vec::new();
    commands.extend(vault_bridge_deposit_relay_commands(
        "propose",
        &plan.propose_operation,
        "PROPOSER_KEY_FILE",
    )?);
    if let Some(operation) = &plan.attest_operation {
        commands.extend(vault_bridge_deposit_relay_commands(
            "attest",
            operation,
            "ATTESTOR_KEY_FILE",
        )?);
    }
    commands.extend(vault_bridge_deposit_relay_commands(
        "finalize",
        &plan.finalize_operation,
        "FINALIZER_KEY_FILE",
    )?);
    commands.extend(vault_bridge_deposit_relay_commands(
        "claim",
        &plan.claim_operation,
        "CLAIMER_KEY_FILE",
    )?);

    let commands_script = vault_bridge_deposit_relay_commands_script(&bundle_dir, &commands);
    atomic_write(&commands_file, commands_script)?;

    Ok(VaultBridgeDepositRelayBundleReport {
        schema: VAULT_BRIDGE_DEPOSIT_RELAY_BUNDLE_SCHEMA.to_string(),
        bundle_dir: bundle_dir.display().to_string(),
        plan_file: plan_file.display().to_string(),
        propose_operation_file: propose_operation_file.display().to_string(),
        attest_operation_file: attest_operation_file.map(|path| path.display().to_string()),
        finalize_operation_file: finalize_operation_file.display().to_string(),
        claim_operation_file: claim_operation_file.display().to_string(),
        commands_file: commands_file.display().to_string(),
        commands,
        plan,
    })
}

pub fn vault_bridge_deposit_relay_rpc_bundle(
    options: VaultBridgeDepositRelayRpcBundleOptions,
) -> io::Result<VaultBridgeDepositRelayRpcBundleReport> {
    let source_rpc_url =
        vault_bridge_non_control_text("--source-rpc-url", &options.source_rpc_url)?;
    let cast_binary = vault_bridge_non_control_text("--cast-bin", &options.cast_binary)?;
    let tx_hash =
        vault_bridge_normalized_hex_text("--tx-hash", &options.tx_hash, EVM_ABI_WORD_BYTES)
            .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;
    let receipt_file = options.bundle_dir.join("source-receipt.json");
    vault_bridge_check_rpc_relay_bundle_outputs(
        &options.bundle_dir,
        &receipt_file,
        options.overwrite,
    )?;

    let receipt = vault_bridge_fetch_cast_receipt(&cast_binary, &source_rpc_url, &tx_hash)?;
    let receipt_transaction_hash = vault_bridge_receipt_hash_field(
        &receipt,
        &["transactionHash", "txHash", "tx_hash"],
        "transaction hash",
    )?;
    if receipt_transaction_hash != tx_hash {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!(
                "source receipt transaction hash `{}` does not match requested tx hash `{}`",
                vault_bridge_0x_hex(&receipt_transaction_hash),
                vault_bridge_0x_hex(&tx_hash)
            ),
        ));
    }
    let receipt_block_hash =
        vault_bridge_receipt_hash_field(&receipt, &["blockHash", "block_hash"], "block hash")?;
    vault_bridge_validate_receipt_success(&receipt)?;
    let receipt_block_number =
        vault_bridge_receipt_u64_field(&receipt, &["blockNumber", "block_number"], "block number")?;
    let block = vault_bridge_fetch_cast_block(&cast_binary, &source_rpc_url, &receipt_block_hash)?;
    let observed_block_hash = vault_bridge_receipt_hash_field(&block, &["hash"], "block hash")?;
    if observed_block_hash != receipt_block_hash {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!(
                "source block hash `{}` does not match receipt block hash `{}`",
                vault_bridge_0x_hex(&observed_block_hash),
                vault_bridge_0x_hex(&receipt_block_hash)
            ),
        ));
    }
    let observed_block_number =
        vault_bridge_receipt_u64_field(&block, &["number", "blockNumber"], "block number")?;
    if observed_block_number != receipt_block_number {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!(
                "source block number {observed_block_number} does not match receipt block number {receipt_block_number}"
            ),
        ));
    }
    let current_block_number = vault_bridge_fetch_cast_block_number(&cast_binary, &source_rpc_url)?;
    if current_block_number < receipt_block_number {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!(
                "source RPC current block {current_block_number} is behind receipt block {receipt_block_number}"
            ),
        ));
    }
    let confirmation_depth = current_block_number
        .checked_sub(receipt_block_number)
        .and_then(|value| value.checked_add(1))
        .ok_or_else(|| io::Error::new(io::ErrorKind::InvalidData, "confirmation depth overflow"))?;

    std::fs::create_dir_all(&options.bundle_dir)?;
    vault_bridge_write_json_file(&receipt_file, &receipt)?;

    let mut plan_options = options.plan_options;
    plan_options.log_file = None;
    plan_options.receipt_file = Some(receipt_file.clone());
    plan_options.observer_confirmation_depth = Some(confirmation_depth);
    let relay_bundle = vault_bridge_deposit_relay_bundle(VaultBridgeDepositRelayBundleOptions {
        plan_options,
        bundle_dir: options.bundle_dir,
        overwrite: options.overwrite,
    })?;

    Ok(VaultBridgeDepositRelayRpcBundleReport {
        schema: VAULT_BRIDGE_DEPOSIT_RELAY_RPC_BUNDLE_SCHEMA.to_string(),
        source_rpc_url,
        tx_hash: vault_bridge_0x_hex(&tx_hash),
        receipt_file: receipt_file.display().to_string(),
        receipt_block_hash: vault_bridge_0x_hex(&receipt_block_hash),
        receipt_transaction_hash: vault_bridge_0x_hex(&receipt_transaction_hash),
        receipt_block_number,
        current_block_number,
        confirmation_depth,
        relay_bundle,
        trust_boundary:
            "source receipt, block, and confirmation depth fetched from the configured EVM RPC and locally validated before generating PFTL relay operations; validators verify the observer-signed facts deterministically and perform no live EVM I/O"
                .to_string(),
    })
}

pub fn vault_bridge_burn_to_redeem_bundle(
    options: VaultBridgeBurnToRedeemBundleOptions,
) -> io::Result<VaultBridgeBurnToRedeemBundleReport> {
    if options.amount_atoms == 0 {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "--amount-atoms must be nonzero",
        ));
    }

    let store = NodeStore::new(&options.data_dir);
    let ledger = store.read_ledger()?;
    let nav_asset = ledger.nav_asset(&options.asset_id).ok_or_else(|| {
        io::Error::new(
            io::ErrorKind::NotFound,
            format!("missing vault bridge NAV asset `{}`", options.asset_id),
        )
    })?;
    if nav_asset.finalized_epoch == 0 {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            "vault bridge asset has no finalized reserve epoch; cannot build burn-to-redeem",
        ));
    }
    if nav_asset.finalized_reserve_packet_hash.is_empty() {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            "vault bridge asset has no finalized reserve packet hash; cannot build burn-to-redeem",
        ));
    }

    let owner_line = ledger
        .trustline_for_account_asset(&options.owner, &options.asset_id)
        .ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::NotFound,
                format!(
                    "owner `{}` has no trustline for vault bridge asset `{}`",
                    options.owner, options.asset_id
                ),
            )
        })?;
    if owner_line.balance < options.amount_atoms {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            format!(
                "owner balance {} is below burn amount {}",
                owner_line.balance, options.amount_atoms
            ),
        ));
    }

    let bucket = vault_bridge_select_burn_bucket(
        &ledger,
        &options.asset_id,
        options.bucket_id.as_deref(),
        options.amount_atoms,
    )?;
    let issuer = options
        .issuer
        .clone()
        .unwrap_or_else(|| nav_asset.issuer.clone());
    let epoch = options.epoch.unwrap_or(nav_asset.finalized_epoch);
    let reserve_packet_hash = options
        .reserve_packet_hash
        .clone()
        .unwrap_or_else(|| nav_asset.finalized_reserve_packet_hash.clone());
    let operation =
        AssetTransactionOperation::VaultBridgeBurnToRedeem(VaultBridgeBurnToRedeemOperation {
            owner: options.owner.clone(),
            issuer: issuer.clone(),
            asset_id: options.asset_id.clone(),
            bucket_id: bucket.bucket_id.clone(),
            amount_atoms: options.amount_atoms,
            epoch,
            reserve_packet_hash: reserve_packet_hash.clone(),
            destination_ref: options.destination_ref.clone(),
        });
    operation
        .validate()
        .map_err(|error| io::Error::new(io::ErrorKind::InvalidInput, error))?;

    let bundle_dir = options.bundle_dir;
    let operation_file = bundle_dir.join("burn-to-redeem.operation.json");
    let commands_file = bundle_dir.join("commands.sh");
    let files = [operation_file.clone(), commands_file.clone()];
    if !options.overwrite {
        if let Some(existing) = files.iter().find(|path| path.exists()) {
            return Err(io::Error::new(
                io::ErrorKind::AlreadyExists,
                format!(
                    "vault bridge burn-to-redeem bundle file `{}` already exists; pass --overwrite to replace it",
                    existing.display()
                ),
            ));
        }
    }

    std::fs::create_dir_all(&bundle_dir)?;
    vault_bridge_write_json_file(&operation_file, &operation)?;
    let commands =
        vault_bridge_operation_relay_commands("burn-to-redeem", &operation, "OWNER_KEY_FILE")?;
    let commands_script = vault_bridge_single_operation_relay_commands_script(
        &bundle_dir,
        "OWNER_KEY_FILE",
        &commands,
    );
    atomic_write(&commands_file, commands_script)?;

    Ok(VaultBridgeBurnToRedeemBundleReport {
        schema: VAULT_BRIDGE_BURN_TO_REDEEM_BUNDLE_SCHEMA.to_string(),
        bundle_dir: bundle_dir.display().to_string(),
        operation_file: operation_file.display().to_string(),
        commands_file: commands_file.display().to_string(),
        owner: options.owner,
        issuer,
        asset_id: options.asset_id,
        bucket_id: bucket.bucket_id.clone(),
        amount_atoms: options.amount_atoms,
        epoch,
        reserve_packet_hash,
        destination_ref: options.destination_ref,
        operation,
        commands,
        trust_boundary:
            "burn-to-redeem bundle is a PFTL holder transaction only; source-chain release still requires finalized PFTL burn evidence, verifier signatures, challenge finality, and direct vault claim"
                .to_string(),
    })
}

fn vault_bridge_select_burn_bucket<'a>(
    ledger: &'a LedgerState,
    asset_id: &str,
    bucket_id: Option<&str>,
    amount_atoms: u64,
) -> io::Result<&'a VaultBridgeBucketState> {
    if let Some(bucket_id) = bucket_id {
        let bucket = ledger.vault_bridge_bucket(bucket_id).ok_or_else(|| {
            io::Error::new(
                io::ErrorKind::NotFound,
                format!("missing vault bridge bucket `{bucket_id}`"),
            )
        })?;
        vault_bridge_validate_burn_bucket(bucket, asset_id, amount_atoms)?;
        return Ok(bucket);
    }

    let candidates = ledger
        .vault_bridge_bucket_states
        .iter()
        .filter(|bucket| {
            bucket.asset_id == asset_id
                && matches!(
                    bucket.status.as_str(),
                    VAULT_BRIDGE_BUCKET_STATUS_ACTIVE | VAULT_BRIDGE_BUCKET_STATUS_IMPAIRED
                )
                && bucket.outstanding_vault_bridge_atoms >= amount_atoms
        })
        .collect::<Vec<_>>();
    match candidates.as_slice() {
        [bucket] => Ok(*bucket),
        [] => Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "no active or impaired vault bridge bucket has enough outstanding supply for this burn; pass --bucket-id after checking vault-bridge-status",
        )),
        _ => Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "multiple vault bridge buckets can satisfy this burn; pass --bucket-id to avoid ambiguous source release",
        )),
    }
}

fn vault_bridge_validate_burn_bucket(
    bucket: &VaultBridgeBucketState,
    asset_id: &str,
    amount_atoms: u64,
) -> io::Result<()> {
    if bucket.asset_id != asset_id {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "vault bridge burn bucket does not belong to the requested asset",
        ));
    }
    if !matches!(
        bucket.status.as_str(),
        VAULT_BRIDGE_BUCKET_STATUS_ACTIVE | VAULT_BRIDGE_BUCKET_STATUS_IMPAIRED
    ) {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "vault bridge burn bucket must be active or impaired",
        ));
    }
    if bucket.outstanding_vault_bridge_atoms < amount_atoms {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            format!(
                "vault bridge burn amount {} exceeds bucket outstanding supply {}",
                amount_atoms, bucket.outstanding_vault_bridge_atoms
            ),
        ));
    }
    Ok(())
}

fn vault_bridge_non_control_text(flag: &str, value: &str) -> io::Result<String> {
    let trimmed = value.trim();
    if trimmed.is_empty() {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            format!("{flag} must be nonempty"),
        ));
    }
    if trimmed
        .bytes()
        .any(|byte| matches!(byte, b'\n' | b'\r' | 0))
    {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            format!("{flag} must not contain newline or NUL bytes"),
        ));
    }
    Ok(trimmed.to_string())
}

