fn vault_bridge_fetch_cast_receipt(
    cast_binary: &str,
    source_rpc_url: &str,
    tx_hash: &str,
) -> io::Result<serde_json::Value> {
    let output = Command::new(cast_binary)
        .arg("receipt")
        .arg("--rpc-url")
        .arg(source_rpc_url)
        .arg("--json")
        .arg(vault_bridge_0x_hex(tx_hash))
        .stdin(Stdio::null())
        .output()
        .map_err(|error| {
            io::Error::new(
                io::ErrorKind::NotFound,
                format!("cast receipt failed to start: {error}"),
            )
        })?;
    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        return Err(io::Error::new(
            io::ErrorKind::Other,
            format!(
                "cast receipt failed with status {}: {}",
                output.status,
                stderr.trim()
            ),
        ));
    }
    if output.stdout.len() > MAX_VAULT_BRIDGE_RPC_RECEIPT_JSON_BYTES {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!(
                "cast receipt output exceeded {} bytes",
                MAX_VAULT_BRIDGE_RPC_RECEIPT_JSON_BYTES
            ),
        ));
    }
    let receipt: serde_json::Value =
        serde_json::from_slice(&output.stdout).map_err(invalid_data)?;
    if !receipt.is_object() {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            "cast receipt output must be a JSON object",
        ));
    }
    Ok(receipt)
}

